  <h2>Om ni stöter på en otrevlig användare som enbart hotar, svär, skriker och inte vill lyssna?</h2>
  Stoisk  artighet kan bara tas till en viss gräns. Har man förklarat för en  användare varför dennes inlägg tagits bort, varför denne inte får ha  nakenbilder eller liknande på ett artigt sätt och denne svarar med  otrevligheter så ignorera honom.<br />
  Om denne däremot inte slutar är man  berättigad blockera honom, eller att sätta en varning, eller ytterst  att ta bort dennes användarkonto om personens attityd och beteende  påkallar en sådan åtgärd.<br />
  <br />
  Exempel på en motivering för en sådan varning kan se ut så här:<br />
  <em>"Otrevlig mot auktoritetsperson på Hamsterpaj. Du ombedes strängeligen  att respektera dina medmänniskor och vårda ditt språkbruk om du vill  fortsätta att nyttja dig utav de tjänster och funktioner som Hamsterpaj  erbjuder."</em><br />
