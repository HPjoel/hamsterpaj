<h2>Varningar:</h2>
<p>  Motiveringen för en Varning skickas till användaren som varnas och syns alltså inte enbart i varningshistoriken.<br />
  Därav räcker det inte att skriva <em>"Spam i forumet, hotat andra användare."</em> eller liknane.<br />
  <br />
  Dock har de som varnas inte betett sig så väl eller följt reglerna särskilt bra så man behöver inte vara jättevänlig.<br />
  Men istället för att bara skriva "Spam i forumet! Skärpning" så skriv något halvt artigt.<br />
  <br />
  Ett exempel på detta kan vara: <br />
  <em>"Varning då du har spammat i forumet. Du uppmanas vänligt men bestämt att läsa forumsreglerna och sluta med spammandet!"</em><br />
  <br />
  Denna  korta motivering beskriver dels vad den varnade användaren har gjort för  fel och hur denne kan bättra sig, samtidigt som det låter relativt  artigt.<br />
  <br />
  <br />
<h2>Borttagning av inlägg i forumet:</h2>

  Undvik att skriva <em>"._."</em> som motivering vid borttagning av inlägg.<br />
  Motiveringarna  är ytterst viktiga då det är främst borttagning av inlägg och de dåliga  motiveringarna som de vanliga användarna klagar på.<br />
  <br />
Ett par exempel som är vänliga så att man slipper missnöjet i lika stor utsträckning:
<ul>
  <li><em> Ditt inlägg var Offtopic. Vänligen läs forumsreglerna så förstår du varför inläget togs bort.</em></li>
  <li><em>Skräpinlägg. Inlägget bryter mot forumsreglerna så läs dem är du snäll.</em><br />
  </li>
</ul>
  <br />
  Vill man göra det lätt för sig så kopierar man hela meningen och byter ut ordet <em>"Offtopic"</em> mot något annat:<br />
  Spam,  skräp, chatt, eller posthunt. På detta viset behöver man inte  sitta och klura ut nya formuleringar för varje nytt inlägg som ska tas  bort.<br />
