<em>Varför finns det en artighetspolicy för de som modererar på Hamsterpaj?<br />
<br />
Ett  längre tag uttrycktes det kritik ifrån de vanliga användarnas håll om att  de som modererade i forumet inte var särskilt snälla, eller att de var på  tok för kortfattade när de korresponderade med de vanliga användarna.<br />
<br />
Detta syftar till motiveringarna som skrevs för borttagna inlägg, rapporthantering och dylikt.<br />
Till exempel skrevs det ofta: "._.", "skräp" eller kanske "posthunt".<br />
Detta ledde tyvärr till att moderatorerna fick kritik och att det blev en klyfta mellan vanliga användare och de som moderade och därav 
infördes artighetspolicyn som är till för att motverka sådant.</em><br />
<br />
<br />
<hr>
<h2><br />
  <br />
Rapporthantering:</h2>

När en rapport tas hand om skriv inte enbart <em>"ordnat"</em> eller <em>"omhändertaget"</em> eller <em>"inlägget borttaget"</em>.<br />
Detta är alldeles för kortfattat och kan uppfattas som otrevligt.<br />
<br />
Istället, skriv lite extra. <br />
Exempel på det lilla extra kan se ut såhär: <em>"Ordnat! Tack för rapporten! :)"</em><br />
Det tar bara någon sekund att skriva det och tonen blir omedelbart mycket trevligare.<br />
<br />
Utvecklarna har full förståelse för att man inte alltid orkar eller vill skriva något sådant här: <br />
<br />
<em>"Inlägget  borttaget då det var offtopic och sådant vill ju inte Hamsterpaj ha så tack jättemycket för att du rapporterade det så att vi kunde ta  bort skräpet!"</em><br />
<br />
Bara den lilla smileyn och "tack för rapporten" sa ju allt det där väldigt kort och koncist.<br />
