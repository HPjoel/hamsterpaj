<p><br /><br />
<em>Indoor Golf Arena</em> är ett projekt som leds ut av deras VD Jon S. Standing. Enligt honom kommer <em>Indoor Golf Arena</em> bli "världens  största träningscenter och diverse redskap för inomhusgolf" inom golf.  Han vill försöka nå ut med budskapet om att <em>”golf är livet”</em> och visst får man säga att detta projekt inte försämrar hans odds när det gäller att göra avtryck i historien inom golfvärlden. 
</p>
<br /><br />
<a href="http://images.hamsterpaj.net/article_illustrations/indoorgolfarena/3_large.jpg" title="Klicka för större bild">
<img src="http://images.hamsterpaj.net/article_illustrations/indoorgolfarena/3.jpg" style="float: left; margin:10px;" />
</a>
<p><br />
<em>Indoor Golf Arena</em> bjuder på högkvalitativ golf 365 dagar om året på en 14 000m2 stor syntetisk gräsplan med 28 stycken utslagshål. En stor golfshop och fittnesscenter finns att tillgå också och det finns till och med en träningsarena samt en liten golfbana för barnen. För den som har tröttnat på golfen ett tag finns det restauranger på toppklass tillsammans med barer och salonger. Bo på <em>Indoor Golf Arenas</em> fyrstjärniga hotell med över 200 rum och fina konferenssalar, kontor och lokaler.
</p>
<br style="clear: both;" />
<p>
<em>Indoor Golf Arena</em> är framtiden och jag skulle inte bli förvånad om det inom loppet av de tio närmsta åren får se ett liknande i Sverige.
</p>
