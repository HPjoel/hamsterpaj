<h2>9 Ingen reklam i forumet!</h2>
<p><br />
  <br />
  Vi  vill inte ha massor av trådar eller inlägg som länkar till andra  communitys, extraerbjudanden i internetbutiker eller liknande.<br />
  <br />
  Har  du gjort en ny snygg hemsida så får du självklart skapa en tråd om det.  Och man får också i förbigående nämna och diskutera erbjudanden som  finns i internetaffärer och länka dit.<br />
  Det vi inte vill  är att man skapar fem trådar på rad om det, skriver reklam om det i vartenda tråd man ser. <br />
  <br />
Ett exempel:<br />
"Titta på min nya snygga hemsida: www.*******.net ! Den är jättecool!" <br />
En tråd om sådant här räcker.<br />
