<h3>Det är jätteviktigt att man läser de här reglerna. De finns till för  att alla ska trivas på Hamsterpaj och för att inte ska bli så mycket  skräp och sådant i forumet.<br />
<br />
Och du, glöm inte bort att det  ibland finns klistrade trådar i vissa forumkategorier där det finns  extra regler för vad som gäller i den kategorin, så läs dem också!</h3><br />
<br /><br />
<hr />
<br />
<br />
<h2>1. Spamma inte och gå inte heller off-topic (gå ifrån ämnet).</h2>
<br />
<br />
Exempel  på Off Topic kan vara att påpeka att någon stavar väldigt dåligt när  tråden handlar om kristendomen eller senaste låten av Fronda.<br />
<br />
Det  kan också vara att någon skriver <em>Jag röker inte!!</em> när tråden handlar  om hur många ungdomar som röker och varför de började röka. Sådana  inlägg tillför inget och har inte med tråden att göra. (Sådant kanske  kan tolkas som posthunting med.)<br />
<br />
Exempel på spam kan vara att skriva riktigt oseriösa saker som bara irriterar.<br />
Till exempel skriva "Visa pattarna" i en tråd där en tjej frågar hur man får en kille att bli intresserad av henne.<br />
<br />
<br />
<h2>2. Posthunting är inte tillåtet.</h2>
<p><br />
  <br />
  Posthunting  kan vara att man skriver "Tack!", eller "jag håller med!" i trådar.  Sådana inlägg fyller ingen funktion och tillför inte tråden något och  de kommer då att tas bort. Gör man så många gånger på rad så får man RO.<br />
  <br />
  Det  är också posthunting då man skriver samma svar i en tråd som någon  redan har sagt. Det behövs inte tjugo personer som säger exakt samma  sak bara och 19 av dem svarar bara att deras inläggsräknare ska gå upp  med 1.<br />
  <br />
Ser du en spamtråd i forumet som ligger och väntar på att  bli borttagen eller flyttad till spamforumet där den hör hemma, svara  inte i den! Det kommer att räknas som posthunting om du skriver i den!  Rapportera den istället för att skriva i den.</p>
<p>Posthunting kan också se ut så här:</p>
<p>Tråden frågar det här: <em>"Hur får man upp aktivitetshanteraren?"</em><br />
    <br />
  Folk svarar: <br />
    <em>"Ctrlaltdel"<br />
"Ctrl + alt + del"<br />
"Testat control alt delete?"<br />
"Haha,  noob" </em><br />
  <br />
  Det är ett bra exempel på posthunt och sådant vill det inte vetas av i forumet. <br />
  <br />
</p>
<h2>3. Lyd dem som moderar, det de säger är det som gäller.</h2>
<br />
<br />
De som moderar är utsedda för att upprätthålla reglerna på Hamsterpaj. Om de säger åt dig  att sluta spamma, att sluta bryta mot reglerna så ska du göra det.<br />
Du har rätt att fråga vad du har gjort för fel, men gör det på ett <strong>artigt</strong> sätt! De är inte skyldiga att vara trevliga mot dig och svara om du bara skriker, svär och förolämpar dem.<br />
<br />
<br />
<h2>4. Följ svenska lagar och regler.</h2>
<p><br />
  <br />
  Det  betyder att man inte får skriva kränkande innehåll i trådarna. Det  innebär också att det inte är tillåtet att skriva saker som kan anses  vara hets mot folkgrupp, hot, ärekränkning eller liknande.<br />
  <br />
  Exempel på detta kan vara att skriva: <em>Döda alla bögar och judar!</em> Den som är dum nog att skriva något sådant omotiverat utan massor av  förklaringar, faktaunderlag och argument i sitt inlägg får hårda straff.<br />
  </p>
<p>Här ingår det också att man inte ska fildela. Man får säga vilka sidor man kan hitta saker på eller vilka program som kan användas för det men inga direkta länkar till torrents eller liknande! <br />
  <br />
</p>
<h2>5. Chatta inte i forumet.</h2>
<br />
<br />
Vill  du snacka med någon så ta det i gästboken. De som skriver och läser i  forumet brukar inte vara intresserade av att se när du chattar med  någon.<br />
<br />
Exempel på detta kan vara: <br />
"Va är inte du bara 15?<br />
Nej, jag är 14.<br />
Okej.<br />
Varför frågar du?<br />
Vet inte, bara undrade."<br />
