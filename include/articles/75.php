<h2>7. Placera trådarna i rätt del av forumet.</h2>
<br />
<br />
Det finns många kategorier av en anledning. <br />
Till  exempel ska trådar som heter "Senaste avsnittet av Heroes" ligga i  kategorin för tv-serier och filmer, inte i Mellan himmel och jord.<br />
<br />
