<p>
	Uppföljaren till Fantastic Foursläpps i Sverige i mitten av Augusti 2007, filmbolaget Fox bjöd över Hamsterpaj
	till London för att se filmen och vara med på galapremiären. Här kommer recensionen av filmen!
</p>

<h2>Betydligt bättre än den första filmen</h2>
<p>
	Den första filmen kändes ganska hafsig och slarvig. Man försökte göra händelserna trovärdiga genom att förklara förändringarna
	med DNA-struktur och andra avancerade ord. Man fick inte känslan av superhjältar från seriernas värld, något som hade gjort att
	man kunde strunta i saker som att det inte finns tyngdkraft i rymden och att en DNS-förändring inte får en att börja brinna.<br />
	I uppföljaren har de lämnat verklighetsgrejen och kör mer på att göra en fartfylld, rolig och maffig actionrulle som känns
	lite som en serieteckning.
</p>


<h2>Handlingen</h2>
<p>
	Det har gått ett tag sedan de fyra superhjältarna räddade världen från von Doom, som nu sitter inlåst långt borta i Litauen.
	Reed och Sue planerar sitt bröllop och Johnny försöker tjäna så mycket pengar som möjligt på superkrafterna och Ben trivs med
	sin nya tjej. Då inträffar något oförklarligt, ett ljussken far fram genom jordens atmosfär. Det får hav att frysa till is och
	belägger pyramiderna i Egypten med Snö.<br />
	The Silver Surfer har kommit till Jorden och han hotar att utplåna planeten med sina övernaturliga krafter. De fyra hjältarna inleder
	en kamp för att rädda jorden och hindra Silverkillen, plötsligt stiger von Doom in på scenen med ovärderlig information: Silverkillens
	krafter sitter i hans surfingbräda.<br />
	Tillsammans ska nu USAs armé, våra fyra superhjältar och den forna skurken von Doom rädda världen.<br />
	Frågan som kvarstår är: Kan man verkligen lita på von Doom?
</p>

