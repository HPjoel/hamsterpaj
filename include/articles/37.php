	kunde inte höra någon rädsla eller upprördhet i makens röst.
</p>

<p>
	Ingenjörer som medverkade vid konstruktionen av tvillingtornet har deklarerat att de var avsedda att klara en
	kollision med Boeing 707 och efterföljande eld. Elden skulle vara den största faran &#8211; många skulle dö, men byggnaden
