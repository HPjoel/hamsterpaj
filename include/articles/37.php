	först i augusti 2005. Under hela eftermiddagen varnades brandmännen för att nr 7 också skulle rasa. Polis motade
	bort folk i närheten av byggnaden från kl. 13. 
</p>

<p>
	BBC rapporterade att 7:an fallit tjugo minuter innan den rasade, byggnaden står kvar bakom ryggen på reportern.
	Fem minuter innan raset bryts kontakten mellan New York och London. När 7:an slutligen faller är det med tydliga
	explosioner och den faller som vid en &#8221;controlled demolision&#8221;, precis som tornen.
</p>

<p>
	World Trade Center arrenderades våren 2001 av affärsmannen Larry Silverstein med ett 99-årskontrakt. Under sommaren
	omarbetades försäkringsvillkoren till att också omfatta terroristaktioner. I arrendeavtalet ingick rätten för Silverstein
	att bygga nytt på platsen om WTC revs. Efter attacken hävdade Silverstein att WTC utsatts för två attacker, eftersom
	det var två plan. Han skulle därför ha dubbel ersättning, vilket han också fick: sju miljarder dollar, en bra
	förräntning av de 15 miljoner han betalat för sitt arrendekontrakt.
</p>

<p>
	Byggnaderna 3, 4, 5 och 6 hade svåra skador efter tornen, men kollapsade ändå inte. Det gjorde däremot 7:an,
	som stod längre bort. Silverstein säger i en intervju att han i ett telefonsamtal med brandkårsledningen sa: 
	&#8221;Let&#8217;s pull it.&#8221; och detta beslutades. Senare påstod anställda hos Silverstein att han avsett brandmännen,
	det var dem man skulle &#8221;pull out of harm&#8217;s way&#8221;. Det var bara det att det fanns inga brandmän i 7:an, de hade
	uppmanats att lämna långt innan.
</p>

