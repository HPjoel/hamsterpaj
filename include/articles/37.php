<h2>Detta vill man att vi skall tro</h2>
<p>
	Redan kl. 10.05 rapporterar CNN och NBC att Osama bin Laden kan ha varit inblandad i attackerna.
	Foto på bin Laden visas gång på gång i TV tillsammans med förstörelsen på nedre Manhattan. Man kan
	likna det vid betingning: tittaren lär sig att koppla terrordåd till Osama bin Laden och när man
	bara ser en bild av honom, eller varför inte en muslim vilken som, så tänker man genast på terror.
	Det ansågs så uppenbart att bin Laden lett attentatet från grottorna i Afghanistan, att en seriös
	undersökning eller bevisföring var överflödig. Osama själv förnekade i en intervju den 28 september
	att al-Qaida skulle ha något med attentatet att göra.
</p>

