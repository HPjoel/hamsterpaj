	visade sig senare vara i livet. Bara några timmar efter attentaten kunde man spåra kaparnas väg
	till planen, detta efter att de lyckats hålla sig undan myndigheterna i åratal. Kaparna skall ha
	varit muslimska fundamentalister, vars starka religiösa motiv fick dem att övervinna alla svårigheter,
	med flygningen t.ex. Ändå skall de ha gått på porrklubb kvällen innan och betalat med kreditkort, samt
	drällt exemplar av Koranen kring sig, ett i en bar och ett i en hyrbil. Ett mycket omuslimskt beteende.
</p>

