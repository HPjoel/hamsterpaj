<h2>Detta skall vi helst inte veta</h2>

<p>
	Flygövervakningen fungerade inte normalt 11 september. De kapade planen lyfte från flygplatser långt från målet
	eller flög en lång sträcka bort från målet innan kaparna tog över. De skulle normalt ha hejdats av flygvapnet,
	vilket kaparna borde ha varit medvetna om. Men&#8230;
</p>

<p>
	FAA (Federal Aviation Administration) tog orimligt lång tid på sig att rapportera till flygvapnet. När man till
	slut reagerade, skickades jaktplan från avlägsna baser, fast det fanns gott om dem på närmre håll. När jaktplanen
	kom iväg, flög de med en bråkdel av sin topphastighet. Det jaktplan som redan var i luften omdirigerades inte.
</p>

<p>
	Officiellt säger NTSB (National Transportation Safety Board) att de &#8221;svarta boxarna&#8221; från planen aldrig återfanns.
	Men en källa från NTSB säger: &#8221;Off the record, we had the boxes. You&#8217;d have to get the official word from the FBI
	as to where they are, but we worked on them here.&#8221;  Två brandmän, Mike Bellone och Nicholas De Masi, hävdade 2004
	att de funnit tre av boxarna på Manhattan och att FBI-agenter tagit dem. FBI förnekar detta. 9/11 Commission Report
	säger i en fotnot att boxarna inte återfunnits och att boxen från flyg 77 var så skadad att man inte fått fram
	någon information. Det är mycket ovanligt att boxarna inte hittas.
</p>

<p>
	Planet som attackerade Pentagon gjorde en avancerad spiraldykning och träffade den del som höll på att renoveras,
	istället för de delar där Rumsfeld och andra högdjur höll till. Piloter har ifrågasatt om ens en skicklig
	stridspilot skulle klara den manövern, den man som påstods flyga det kapade flyg 77, Hani Hanjour,  saknade
	definitivt den kompetensen.
</p>

<p>
	Det finns många vittnesmål om explosioner även vid Pentagon. Vittnen uppger sig också ha känt lukten av kordit (ett sprängämne).
</p>

