<h3>Om du ändå känner dig osäker så har vi ett tomat-tips som faktiskt fungerar ganska bra</h3>
<ol>
<li>Skaffa en fin och fräsch tomat, lagom mogen och lagom stor</li>
<li>Skär av toppen på tomaten, så att du får ett drygt en centimeter tjockt "lock"</li>
<li>Använd fingrarna för att ta bort "väggarna" i tomaten</li>
<li>Häll ut ungefär hälften av kärnorna, så att tomaten inte är lika blöt och kladdig</li>
<li>Nu är det bara att börja kyssa!</li>
</ol>
<p>
Givetvis blir det en kall och känslolös kyss, men tekniken fick ändå mellan 5-7 poäng av tio när vi bad några hamsterpajare testa!
</p>


<h2>Vad är det för skillnad på kyssar och hångel?</h2>
<p>
Hångel är mer än en kyss: hångel är kyssar och smekningar, ofta ganska intima sådana. En kyss kan vara ett sätt att säga "hejdå" på, hångel tar tid, att hångla i en timma är inget konstigt.<br />
Ofta hånglar man liggandes, i en soffa eller kanske en säng. Det börjar lite trevande som en kyss och slutar många gånger med att varma kläder åker av och att det är händer lite varstans; på rumpan, insidan avlåren, i håret, på tjejens bröst och bak i nacken.<br />
Och sist, lite etikettregler:<br />
<strong>Att kyssas bland andra är okej - att hångla offentligt är inte okej!</strong>
</p>

<h2>Vad kan man göra för att bli en bättre kyssare då?</h2>
<p>
Ett mantra som stål att upprepas är <em>"alla är olika"</em>. Det är svårt att hitta två killar eller två tjejer som gillar precis samma saker. Att kyssas "bra" handlar om att göra just det din kysspartner gillar, det är något man lär sig med tiden. Man märker vad som skickar kårar längs nacken, vad som får nadhämtningen att bli djupare, när hon/han slappnar av ännu mer och hur man ska locka fram de där knappt hörbara små stönen.
</p>
<h3>Men, ett par praktiska tips som fungerar på många</h3>
<ul>
<li>Använd en hand för att hålla om din partners nacke, hår eller käkparti</li>
<li>Våga visa att du njuter! Många verkar tycka att det är pinsamt att visa känslor, det är det inte!</li>
<li>Kyss gärna utanför munnen, halsen och vägen upp mot baksidan av örat är känslig</li>
<li>Lätta smekningar och mjuka "nafsande" kyssar mellan halsen och nacken brukar många tycka om</li>
</ul>