<h2>Hjälp, jag har aldrig kysst någon förut!</h2>
<p>
Du  är inte ensam, det finns de som är tjugo och aldrig har kyssts. Det kommer förr eller senare och är inget att oroa sig för. När man är ung är det lätt att känna sig lite efter och oerfaren om "alla andra" har haft flick-/pojkvän, hånglat och varit fulla. Fast det är inte ovanligt att klasskompisar och vänner "drar en vit lögn" för att verka mer erfarna än de egentligen är. Ingen vill ju framstå som efter.<br />
Och visst blir det mer och mer press när många låtsas vara erfarna, eller hur?
</p>

<p>
Och du, förresten, det märks inte om du har kysst någon förut eller inte, så du behöver inte oroa dig.
</p>


<h2>Hur gör man då?</h2>
<p>
Att försöka beskriva en kyss för någon som aldrig kyssts är inte det lättaste. En kyss är ju mycket mer än en djup puss. Jag ska göra ett försök här, men ingen text i världen kan förklara hur det går till och hur det känns.
</p>

<p>
Man låter läpparna mötas, de flesta sluter ögonen, och sedan öppnar man munnen. Många stänger och öppnar munnen i en långsam, lite "nafsande" rörelse. Ibland kan man låta tungorna mötas, kanske cirkulera runt varandra ett tag innan man stänger munnarna igen.
</p>

<p>
En kyss kan vara mellan två-tre sekunder och flera timmar, även om det vanligaste är någonstans mellan fem sekunder och en minut.
</p>

<p>
Hur man gör när man kysser varandra sitter någonstans långt in i ryggraden på alla. När det väl är dags så bara fungerar det. Efter någon sekunds kyssande förstår man hur det fungerar - och skulle man känna sig osäker är det bara att "följa med". En kyss är 90% känslor och 10% teknik.
</p>

