<h2>Socker-salpeter brinner med en väldigt varm låga och är svårt att släcka</h2>
<p>
Bladningen smälter lätt genom en aluminiumburk när den brinner, och kan vara svår att släcka. Gör detta utomhus på asfalt, berg eller grus.
</p>
</div>

<p>
Denna blandning blir ett vitt, svårtänt pulver som brinner mycket intensivt och utvecklar en tjock vit rök när det väl brinner. Det är inte explosivt och egentligen inte farligare än en brinnande tidning.
</p>

<h2>Detta behöver du</h2>
<ul>
<li>Tomtebloss</li>
<li>Salpeter (köps i större mataffärer, står bland kryddorna. Salpetersalt fungerar inte)</li>
<li>Vanligt strösocker</li>
<li>Tom aluminiumburk (typ Coca Cola eller folköl)</li>
</ul>

<h2>Så här gör du</h2>
<ol>
<li>Mät upp lika delar socker och salpeter i en skål eller bunke.</li>
<li>Skär av toppen på aluminiumburken, skölj ur den och torka</li>
<li>Häll över blandningen av socker och salpeter i aluminiumburken</li>
<li>Tänd tomteblosset och använd detta för att tända rökblandningen</li>
</ol>

<h2>Tips</h2>
<ul>
<li>Med större andel socker bildas mer rök, men blandningen blir svårare att tända, testa med 2/3 socker.</li>
<li>Tänd inte eld på blandningen inomhus!</li>
<li>Detta är en väldigt stabil blandning, den kan inte självantända och det finns ingen risk att den exploderar.</li>
<li>Om du inte får tag i tomtebloss kan du tända blandningen med toppen på en tändsticka. Fast det är inte alltid det är så lätt.</li>
</ul>