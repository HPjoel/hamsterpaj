<h2>När är det färdigt?</h2>
<p>
	Till att börja med så skall allt kött ha minst rumstemperatur när man börjar grilla. Om du har fryst kött så se till att ta fram det på morgonen eller dan innan om du misstänkar att det är grillning på gång. Om du har köttet i marinad så plocka upp köttet ur påsen och torka av det med papper innan du lägger det på grillen. Marinaden har gjort sitt och skall bort. Om du är rädd för att köttet skall fastna på gallret så kan du pensla på lite olja på köttet eller på gallret. Snåla med oljan! Om det börjar droppa olja så börjar den brinna och när det brinner med låga under köttet så blir det bränt men inte färdigt. Man blir hungrig av att vänta och när man väl ätit får man cancer. Vänd köttet när köttsaften börjar tränga upp och lägger sig ovanpå. Grilla inte för nära glöden, då får du samma effekt som när du sölat olja. Om du grillar med engångsgrill så tänk på att du max klarar att grilla två omgångar med köttskivor på en grill. Har du otur och får ett lite sämre exemplar så tar glöden slut tidigare. Köp därför ordentligt med grillar om ni är många. Det går att grilla ca tre skivor kött åt gången på en engångsgrill och kanske fyra spett. Träng inte ihop för mycket kött på grillen, då blir temperaturen för låg och inget blir färdigt, glöden slocknar och folk blir sura. Här följer ungefärliga grilltider:
	<ul>
		<li>Lövbiff, ca 1 min, grilla bara på ena sidan.</li>
		<li>Biff,entrc&ocirc;te 	2-3 min/sida</li>
		<li>Fläskkarré, 3-4 min per sida.</li>
		<li>Kotlett, 3 min per sida.</li>
		<li>Spett med fläskkött, 6-8 min.</li>
		<li>Bröstfilé av kyckling, ca 20 min, vänd några gånger.</li>
		<li>Lårfilé av kyckling, 4 min per sida.</li>
		<li>Kyckling i bitar 	10-12 min/sida</li>
		<li>Grönsaksspett, 8-10 min.</li>
		<li>Grillkorv  	4 min</li>
		<li>Hamburgare 	2 min/sida</li>
		<li>Flintastek 	10-15 min/sida (mer än vad en engångsgrill klarar av)</li>
	</ul>
</p>
<p>
	Fläskkött är färdigt när det inte längre känns mjukt och svampigt. Använd grilltång eller pincett för att känna på köttet. Börja peta redan efter att du vänt på köttet första gången så att du sedan känner skillnaden när köttet blivit hårdare. Det är inte panik om fläskkött är lite rosa. Det finns nu för tiden inte en massa farliga saker i grisen som man dör eller blir illamående av. Däremot tycker många fortfarande att det är otrevligt med inte helt genomstekt fläskkött så ofta är det bäst att se till att det verkligen är det. När alla skivor känns färdiga så skär ett litet snitt i den tjockaste och kontrollera att köttsaften är klar och inte rosa. Grilla inte längre än nödvändigt för då blir det bara torrt och tråkigt.
</p>
<p>
	Nötkött vill folk äta väldigt olika välgrillat så fråga folk hur dom vill ha det eller grilla allt till att vara lite rosa men inte rött. Det brukar passa de flesta.
</p>
<p>
	Kyckling måste lagas helt igenom. Här kan finnas massor av otäckheter som man måste se till att ha ihjäl och det vet man har hänt när inget längre är rosa eller rött. Även små blodkärl och annat som kan sitta i kyckligbiten måste vara lagat så att det tappat sin färg.
</p>
