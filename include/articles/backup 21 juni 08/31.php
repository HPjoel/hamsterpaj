<h1 style="clear: both;">En sammanfattning och lite blandade tips</h1>
<p>
	<ul>
		<li>
			Om du handlar på vägen till stranden och inte hittar (eller inte vill ha) färdigkryddat kött så köp en rulle plastpåsar också (typ fryspåsar). När du kommer ut ur affären så plocka upp en påse, slit upp köttförpackningen och lägg köttet i påsen. Riv upp marinadpåsen och häll i marinaden i påsen med köttet. Knyt igen påsen och försök att lämna kvar så lite luft som möjligt i påsen. Knåda påsen lite försiktigt och blanda rund kött och marinad så att all köttyta får marinad på sig. Lägg sedan påsen i en eller två extra plastpåsar så minskar risken att du får köttmarinadkladd i ryggsäcken.
		</li>
		<li>Ge folk något att tugga på medan dom väntar</li>
		<li>Snåla inte med tändvätskan</li>
		<li>Vänta tills glöden är färdig. Tills kolen är grå.</li>
		<li>Grilla inte kallt kött</li>
		<li>Torka av marinaden</li>
		<li>Grilla inte för nära glöden</li>
		<li>Vänta tills köttsaft börjar tränga upp innan du vänder</li>
	</ul>
