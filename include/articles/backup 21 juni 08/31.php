<p>
	<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/tallrik.jpg" style="margin: 0px; display: block;" />
	Så fort våren kommer så kan man höra diskussionerna vid köttdisken. 
	<ul style="list-style-type: none;">
		<li>&ndash; Va e de för kött man grillar nu då?</li>
		<li>&ndash; Ingen aning. De brukar alltid va nån annan fixar sånt.</li>
		<li>&ndash; Kolla här! 'Grillskiva - färdigkryddad'. Det blir bra, den tar vi.</li>
		<li>&ndash; Ok, men va äter man till då?</li>
	</ul>
	Den här guiden svarar på alla dessa frågor och visar hur man kan grilla på ett enkelt sätt. Jag tillhör visserligen dom som gärna vill krångla till det och göra en massa avancerade tillbehör och såser hit och dit. Detta slutar dock oftast med att kompisarna hinner bli griniga av hunger och jag blir trött av stress. Av detta kan man lära sig dom två viktigaste sakerna när det gäller grillning: 1. Krångla inte till det. 2. Se till att det finns något att äta medan man väntar. Grillning tar alltid längre tid än man tror så om man har något att tugga på under tiden så blir det en trevligare kväll. Köp till exempel ölkorv och chips som folk kan äta på så länge. Detta gäller både när man grillar hemma och när man släpat med sig engångsgrillar till stranden eller parken.
</p>

<h1>Vad grillar man?</h1>
<p>
	Det mesta som kan tillagas i stekpanna eller i ugn går också att grilla men allt blir inte bra och mycket tar alldeles för lång tid. Entrecote, ryggbiff, lövbiff, fläskkarré, kotlett och skinkschnitzel går alla bra att grilla i skivor och funkar även på engångsgrill. Vill man grilla kyckling så går det bra både men lårfiléer och bröstfiléer. Att grilla hela kycklingbitar med ben är visserligen väldigt gott men tar lång tid och är allmänt krångligt.
</p>
<h1>Vad behöver man?</h1>
<p>
	Vi börjar med en enkel checklista som du kan använda för att kolla att du har tillräckligt med grejer:
	<ul>
		<li>Något som skall grillas (kött och grönsaker).</li>
		<li>Något att elda i (typ en grill).</li>
		<li>Något att elda som glöder länge (grillkol eller helst bricketter).</li>
		<li>Något som går att tända eld på och som brinner mycket (typ tändvätska).</li>
		<li>Något att tända eld med (tändstickor eller tändare).</li>
		<li>Något att äta till det som grillats (potatissallad och bröd, ev grönsallad).</li>
		<li>Något att äta på och med (tallrik och bestick).</li>
		<li>Något att dricka.</li>
		<li>Något att dricka ur. (nödvändigt om förra punkten var i trelitersförpackningar men inte om det var en back lättöl.)</li>
		<li>Något att lägga allt skräp i.</li>
		<li>Någon att grilla tillsammans med. (alt Någon att grilla om man är polare med Hannibal.)
	</ul>
</p>
<h1>Recepten</h1>
<p>
	För att det skall bli så lätt som möjligt att ordna en god och trevlig kväll så får du här några olika förslag på kompletta grillmåltider. Alla är utprovade av mig tillsammans med släkt och vänner under många års grillande.
</p>
<h2>Grillspett med potatissallad och aioli</h2>
<h3>eller Georgios grillspett, Lindas potatissallad, Leifs ailoi och ett grönsaksspett</h3>
<h3 style="clear: both;" >Georgios grillspett</h3>
<div style="float: right; margin: 5px;">
	<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/skinkstek.jpg" />
	<br />
	<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/gronsaksspett_ingredienser.jpg" />
	<br />
	<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/gronsaksspett.jpg" />
</div><p>
	Den här rätten är en kombination av tre stulna recept. För väldigt länge sedan när jag sprang omkring med ett trägevär i händerna och skrek "Du är död!" så fick jag ibland smaka på min kompis pappas fantastiska grillspett. Skär upp en skinkstek i bitar som är ca 3 cm tjocka. Trä på bitarna på ett spett och krydda med oregano, bara(!) oregano. Vänta med salt tills efter grillningen. Trots den sparsamma kryddningen så för köttet väldigt mycket smak och resultatet är ett fantastiskt grillspett.
</p>
<p>
	När vi ändå håller på att göra spett så passar vi på att göra ett med grönsaker också. Om inte annat så är det bra att ha något annat än sallad att bjuda eventuella veganer och vegetarianer på. Fruktianer kan jag tyvärr inte tillgodose här. Jag ber här att få infoga ett citat från en kompis till mig: "Om djur inte är till för att ätas, varför är dom då gjorda av kött?"
	<br />
	Skär upp ca 3 cm bitar av paprika i olika färger, zuccini och aubergine och trä allt på ett spett tillsammans med körsbärstomater och eventuellt champinjoner. Pensla spetten med olivolja och strö på torkad timjan och salt.
</p>
<h3 style="clear: both;" >Lindas potatissallad</h3>
<p>
	Senare i livet blev jag av en kompis dåvarande flickvän bjuden på en potatissallad som också är mycket enkel och lika god. Det enda som tar tid här är att koka potatis (gärna färskpotatis) och vänta på att den skall svalna innan man kan skär upp den i mindre klyftbitar.
	<div style="float: right; margin: 5px;">
	<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/potatissallad_ingredienser.jpg" />
	<br />
		<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/potatissallad.jpg" />
	</div>
	<ol>
		<li>
			Borsta av och tvätta potatisen (skala om det är tjockt skal), lägg i kastrull och täck med vatten. Lägg i ca 1 tsk salt per liter. Koka i 15-20 minuter. Hur man testar om potatisen är färdig lärde du dig väl i hemkunskapen?
		</li>
		<li>
			Bryt loss bladen på en kruksallad från roten och skölj i kallt vatten. Detta är viktigt för att slippa diverse främmande ämnen som hamnat på salladen under produktion och transport. Och så slipper man få jord i munnen.
		</li>
		<li>
			Bryt och riv sedan isär bladen i mindre bitar och lägg i en skål (eller en låda du kan packa ner och ta med till stranden). Om man bryter istället för att skära så håller sig småbitarna friska längre.
		</li>
		<li>
			Nyp av hela basilikablad och lägg i skålen.
		</li>
		<li>
			Klipp av några kvistar med färsk timjan och lägg i skålen.
		</li>
		<li>
			Dela en rödlök på mitten, ta bort det torra skalet och skär tunna skivor. Lägg de halva ringarna du får i skålen.
		</li>
		<li>
			Häll i massor av små gröna oliver (det är den sort som passar de flesta, Euroshoppers små gröna är helt ok. Vill du vara lite mer avancerad kan du köpa stora svarta med kärna, de är för dom som verkligen gillar oliver.)
		</li>
		<li>
			När potatisen svalnat skär du upp den i mindre klyftor. Små potatisar i fyra klyftor. Stora potatisar kan du dela i åtta bitar genom att först klyva på längden två gånger och sedan en gång på tvären.
		<li>
			Strö på lite salt.
		</li>
		<li>
			Blanda.
		</li>
	</ol>
</p>
<h3 style="clear: both;" >Leifs aioli</h3>
			<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/aioli.jpg" style="float: right; margin: 5px;" />
<p>
	Ta nu fram en skål och lägg i 2 dl creme fraiche och 3 dl gräddfil. Hacka upp en vitlöksklyfta i mycket små bitar och klipp i en näve persilja. Strö över lite salt. Denna aioli har jag doppat många friterade potatisklyftor i på en sommarkrog i min hemstad. Detta är naturligtvis inte en riktig aioli. Aioli är mer en majonäs med vitlök. Sådana här röror kryddade med vitlök kallas dock ofta aioli på svenska resturanger.
</p>

<h2 style="clear: both;">Fläskkarré med krämig potatissallad</h2>
<p>
		<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/marinad.jpg" style="float: right; margin: 5px;" />
	Detta är förmodligen den mest tillagade rätten på svenska engångsgrillar näst efter vanlig grillkorv förstås. Köp fläskkarré i skivor, antingen vanliga eller färdigkryddade och marinerade. Om du köper dom okryddade så köp en påse marinad också. Det brukar finnas några olika smaker att välja mellan. Påsen som det står "Allround" på är den som ger den klassiska grillkryddesmaken. Till köttet passar vanlig enkel potatissallad som finns färdig och klar i plastförpackning i kyldisken. Här kommer ett vanligt problem. Hur mycket potatissallad skall man köpa? Nånstans mellan 200 och 300 gram per person. En datanörd kanske äter 200g om han är hungrig medan en sportfåne lätt äter 300g. Om man vill kan man komplettera med någon grönsallad och några brödbitar men man klarar sig bra utan det också.
</p>
<p>
	Klart! Inget mer behövs, bara att slänga köttbitarna på grillen.
</p>
<h2>Lövbiff med sallad</h2>
<p>
	Här kommer en rätt med endast fyra ingredienser och minimala förberedelser:
			<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/sallad.jpg" style="float: right; margin: 5px;" />
	<ul>
		<li>Lövbiff</li>
		<li>Plocksallad/lösviktsallad</li>
		<li>Rödvinsmarinad (färdig i påse)</li>
		<li>Bröd</li>
	</ul>
	Lägg lövbiff i en plastpåse och häll på marinaden. Rödvinsmarinaden ger en kraftig och mustig smak. I de flesta större mataffärer finns numera sallad av olika sorter i lösvikt. Olika sorters salladsblad och liknande ligger i öppna lådor ungefär som lösgodis. Ta en näve av varje och lägg i samma påse och skaka om lite. Detta brukar vara ganska rent så man kan chansa på att hoppa över att skölja allt i vatten. Då slipper man ju åka hem mellan affären och stranden också. Köp lite bröd och en engångsgrill så är du färdig att bjuda på en gormetmåltid i parken. Men du, ställ inte engångsgrillen på gräsmattan i Humlegården eller Slottsskogen! Den går sönder. Gräsmattan alltså. Eftersom lövbiffen är så tunn så tar den bara 1-2 minuter att grilla så man kan riva av ett helt gäng på en liten engångsgrill. Dessutom slipper man vänta så förbannat länge.

</p>

<h2>Banan med choklad i</h2>
					<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/banan.jpg" style="float: right; margin: 5px;" />
<p>
	När man grillar finns bara en given efterrätt och den är så given att folk blir sura om den inte är med på menyn (i alla fall blir jag det). Köp bananer, choklad och aluminiumfolie. Lägg ut bananerna och skär ett snitt i sidan på varje banan. Skär nästan ända ner till skalen på undersidan. Bryt bitar av en chokladkaka och stoppa ner i klyftan. Eftersom jag är lite av en chokladofil så skulle jag här kunna argumentera för en viss kvalitet på chokladen. Dock är det så att när choklad skall ätas i smält form så är det inte lika noga. Marabous mörka choklad duger därför utmärkt. Det går till och med bra med mjölkchoklad om man gillar den smaken. Lägg sedan bananen på en bit aluminiumfolie och vik upp på båda sidor och vik och kläm ihop längst upp så att du lämnar en luftficka under folien och ovanför chokladen. På det viset fastnar inte hälften av chokladen på folien och det är lätt att kolla om bananen är färdigt genom att riva en litet hål i folien. Om man grillar på stranden eller i parken så behöver man en särskild engångsgrill för varje omgång med bananer, det tar 20-30 min att få bananerna färdiga på en engångsgrill så tänd banangrillen samtidigt som köttgrillen och lägg på bananerna samtidigt som köttet. Då är bananerna klar när man stoppat i sig köttet och inte kan vänta på att få stoppa i sig en rejäl dos onödiga kolhydrater. Om man grillar hemma eller orkar släpa på en kylväska så är det inte alls dumt med glass till bananerna. Bananerna är färdiga när chokladen har smält.
</p>
<h1>Hur gör man?</h1>
<p>
	Hur gör man nu för att köttet skall blir grillat utan att blir bränt, kokt eller att folk hinner bli sura på att dom inte får mat? Jo, man ser till att sätta igång grillen en halvtimme innan man vill äta. Har man släpat med sig allt till stranden så är det bara att packa upp engångsgrillarna och tända dom med en gång. 
</p>
<h2>Hur tänder man?</h2>
<p>
	I denna fråga finns lika många experter som åsikter och tillvägagångssätt. Vad gäller engångsgrillen så är det bara att tända och vänta. Har man inte haft tur och fått ett av de bättre exemplaren så kan man behöva hjälpa till lite. Vänta då tills lågorna försvunnit och använd en tidning, frisbee eller liknande för att blåsa på elden. Tänk på hur du skulle använda en tidning för att fläkta dig i ansiktet och gör på samma sätt men ta i av bara helvete! Ställ dig gärna gränsle över grillen, håll tidningen i båda händerna och svinga den mellan benen och över grillen för att verkligen blåsa liv i den glöd som finns. Var uthållig! Fläkta i en minut eller så och låt sedan grillen klara sig själv en stund tills kolen är grå.
</p>
			<img src = "http://images.hamsterpaj.net/article_illustrations/grillguide/grillgrejer.jpg" style="float: right; margin: 5px;" />

<p>
	Om du skall grilla kött på en vanlig grill så använd grillbricketer i stället för vanlig grillkol. Bricketter glöder bättre och längre så att du hinner grilla allt, även det som är lite tjockare.
</p>
<p>
	Olika sätt att tända en grill:
	<ul>
		<li>
			Eltändare - ett bra sätt att tända utan öppen eld. Häll i ett lager med bricketter, lägg tändaren ovanpå och fyll på med mer bricketter. Koppla in tändaren i eluttaget och vänta ca 20min. Koppla sedan ur tändaren ur uttaget och ta ur tändaren ur grillen. Var dock väldigt försiktig med var du lägger den eftersom den är väldigt varm (vilket tydligt framgår av dess orangeröda färg).
		</li>
		<li>
			Tändblock - skräp! Funkar bra för att tända en brasa men inte en grill.
		</li>
		<li>
			Tändkuber - suveräna! Jag hittade en gång en ask med vita tändkuber. Dom såg ut som små stearinklumpar ungefär men brann löjligt bra. Hittar du såna så räcker det med ett par stycken mitt i högen med bricketter. Tänd på kuberna och vänta.
		</li>
		<li>
			Tändvätska - en klassiker. Det där man alltid hört att man skall spruta på långt före och låta kolen dra i sig tändvätskan i 20 minuter är bara strunt. Spruta på drygt en deciliter tändvätska (snåla inte) på en hög med bricketter och tänd med en gång. Det blir fin glöd på drygt 20 minuter.
		</li>
		<li>
			Grillpåse - allt i ett. En påse med färdigbehandlade bricketter som fungerar väldigt bra. Man lägger hela påsen i grillen och tänder den i alla fyra hörnen. Ger perfekt glöd efter drygt 20 minuter. Detta är ett bra alternativ att ta med sig till stranden om man har en liten bärbar grill.
		</li>
	</ul>
</p>
<h2>Vänta!</h2>
<p>
	Det ständiga problemet när man grillar är att folk inte väntar på att glöden är färdig och det är tillräckligt varmt för att börja grilla. Att i efterhand börja blåsa på grillen, bli röd i ansiktet och sota ner köttet är bara löjligt och man visar att man inte har grillat förut. Eller att man försökt grilla varenda sommar i hela sitt liv men fortfarande står framför husvagnen med ölburk och ölmage och tycker att man är fan så mycket bättre på det här än alla andra kärringar som inte begriper sig på grillning.
<br />
Det är dags att grilla när nästan all kol är grå.
</p>

