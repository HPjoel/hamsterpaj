Du befinner dig ute i öknen, i flera dagar har du strövat mot horisonten i hopp om att hitta en stad eller en oas. Ditt vatten är slut och dina krafter är på upphällningen. Precis då ser du en gammal man som sitter vid en stig i fjärran.
<br />
Åt ena hållet leder stigen till staden Allah-Akhbar där det finns vatten och direktflyg till Sverige. Åt andra hållet är det fem dagars vandring till Mullah-Omar, en ful grotta.<br />
Du vet att den gamle mannen ljuger varannan dag - och varannan dag talar han sanning. Du får bara ställa en fråga till mannen och du vet inte vilken dag det är.
<br />
<h3>Vilken fråga ställer du?</h3>
<div id="riddle_answer">

<h1>Svaret</h1>
<p>
Fråga den gamle mannen <em>"Om jag skulle fråga dig efter vägen imorgon, vilken väg skulle du då säga att leder till staden?"</em>.
</p>

<ul>
<li>
Talar mannen sanning idag kommer han att peka på fel väg imorgon.
Eftersom han är ärlig idag pekar han på den väg han skulle svara imorgon, alltså fel väg.
</li>
<li>
Om mannen ljuger idag kommer han att peka på rätt väg imorgon när han talar sanning.
Men eftersom han ljuger idag så kommer han att peka på fel väg.
</li>
</ul>
<p>
Mannen kommer alltså att peka på fel väg, oavsett om han ljuger eller inte. Börja vandra åt motsatt håll och du är snart framme i staden!
</p>
