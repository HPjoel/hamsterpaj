<h2>Så här gör du</h2>
<ol>
<li>Vik en mittlinje som du sedan viker upp igen. Linjen ska vikas längs med, och inte tvärs över pappret</li>
<li>
Vik ner ena kortsidan cirka en centimeter. Vik än en gång och upprepa tills du har fem vikningar
</li>
<li>Vik in hörnen till mitten, så att det bildas en tjock och lite knölig spets</li>
<li>Vik ihop planet längs mittlinjen, de invikta hörnen ska hamna på insidan. Detta kan bli lite knöligt, men var noggrann och släta ut alla veck.</li>
<li>Nu är det dags att vika ner vingarna. Vik en linje från en punkt cirka en centimeter upp från nosen och låt linjen sluta ungefär två centimeter nedanför toppen på stjärten. Upprepa med andra vingen</li>
</ol>