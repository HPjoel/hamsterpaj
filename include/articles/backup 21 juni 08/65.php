<p>Vi på Hamsterpaj tycker att det mesta är okey men för att sidan skall vara en trevlig plats för alla att vistas på har vi några regler som gäller för bilder och forumet.</p>  	

<h2>Bilder</h2> 
<p>Det kan skilja från fall till fall vad som är tillåtet men syftet är att inga bilder skall kunna väcka anstöt mot någon.</p>
 	 	
<p>
<strong>Är man 14 år eller yngre</strong> tycker vi inte att man ska ha bilder på sig själv som kan uppfattas som sexuellt utmanande, man blir ju ändå inte byxmyndig förrän man är 15 år! Är man 15 år eller äldre är det tillåtet att visa upp sig men då får inte några nakna bröst (där man kan se hela eller delar av vårtgården) eller könsdelar synas. Bilder där man i vanliga fall hade sett genitalierna men där de har blivit bortredigerade är inte heller tillåtet. Rumpor måste täckas till minst hälften.
</p>

<p>
<strong>Vi vill inte ha några bilder där vapen och våld glorifieras,</strong> men självklart är det helt okey att ha kort på ens gevär om man sysslar med skytte eller sina Globalknivar om man är kock. 	
Det samma gäller med droger och liknande (som kanyler), det får inte förekomma i ett hyllande syfte men annars är det okey.
</p> 	 	

<p>
<strong>Dessa regler för bilder gäller överallt på siten där man själv kan ladda upp material.</strong> Utöver det finns även några specialregler för visningsbilder. Dessa står med där man laddar upp visningsbilder. 	
</p>
 	 	
