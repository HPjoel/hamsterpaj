<h3>Kim Jong-Il, Nordkorea</h3>
<p>
	Det här är mannen som är känd för sin högt uppkammade frisyr och sina platåskor. Han väljer denna utstyrsel för att
	se längre ut än sina 160 cm. Sånt	är viktigt inom politiken. Är man en stor ledare måste man också se stor ut.
</p>
<p>
	Ännu viktigare är att man är populär bland folket och det
	vet Jong-Il hur man blir. Det börjar redan vid födseln. Enligt sovjetiska källor föddes "den käre ledaren", som han kallas i Nordkorea,
	i ett arméläger i Sovjetunionen. Nordkoreanerna håller inte med, det vore en alldeles för simpel födsel för den käre ledaren.
	Enligt deras böcker föddes
	Jong-Il på det heliga berget Paektu. Vid hans födsel tändes en ny stjärna och dubbla regnbågar syntes på himmelen. En fågel
	kvittrade att nu hade en stor ledare fötts.
	Tyvärr är det svårt att avgöra vilken version som stämmer.
</p>
<p>
	Som Nordkoreas ledare håller han sitt land isolerat från omvärlden, bygger kärnvapen, styr en av världens största försvar,
	och lever livet. Jodå, Kim Jong-Il gillar lyx. När han åkte tåg genom Ryssland (han är flygrädd) flögs det färsk hummer till honom varje dag.
	Den åt han med silverpinnar medan han sköljde ner stora mängder champagne tillsammans med en samling unga, vackra kvinnor.
</p>
<div class="illustration">
	<img src="http://images.hamsterpaj.net/article_illustrations/typer/jong-il.png" />
	Kim Jong-Il festar loss
</div>
<p>
	Hemma i Nordkorea brukar Jong-Il befinna sig i ett av sina palats. Han har alltså flera. Dit specialbeställer han japansk sushi och
	iransk kaviar. Dessutom rekryteras
	tonårstjejer för att underhålla diktatorn och hans maktelit sexuellt. Dessa flickor kan sedan giftas bort till officerare med
	lägre status när de blivit för gamla, runt 20 sådär.
	Förutom god mat och gott sällskap har diktatorn också gott om pengar. Hans årliga inköp av Hennessykonjak (nåt dyrt slask)
	brukar ligga runt 5 miljoner kronor. Mums. Medelinkomsten på ett år för en Nordkorean är förresten 7 000 kronor.
</p>
<p>	
	Men Jong-Il är inte
	självisk. Med pengar från droghandel, vapenaffärer, penningtvätt och internationell hjälp köper han fina presenter till sina
	vänner. Det brukar bli stora pianon
	och Mercedes Benzbilar. Och en filmsamling på 20 000 hollywoodfilmer till honom själv.
</p>
<p>
	Medan folket i landet svälter njuter alltså ledaren utav sina lyxvanor. Men vad är man inte villig att
	uppoffra för en ledare som, enligt sägen, flyger jetflygplan, skriver operor, producerar filmer, är världens främste poet, och dessutom
	lyckats slå 11 hole-in-ones under sin första golfrunda någonsin. Jojomensan, Kim Jong-Il är en stor man. Det är därför hans födelsedag, den där dagen
	då dubbla regnbågar syntes på himlen,  är helgdag i landet och firas som om det vore nyårsafton.
</p>
<p>
	5 poäng till den som listar ut vem som kontrollerar medierna i Nordkorea.
</p>

<h3>Saparmurat Nijazov, Turkmenistan</h3>
<p>
	Ännu en härlig kille som verkar tro sig vara guds gåva till folket är Saparmurat Nijazov. Han var president i Turkmenistan
	från det att Sovjetunionen föll fram till sin död 2006.
</p>
<div class="left_illustration">
	<img src="http://images.hamsterpaj.net/article_illustrations/typer/Niyazov.png" />
	Saparmurat Nijazov
</div>
<p>
	Nijazov kände att det inte fanns en egen turkmensk kultur. Så den tyckte han kunde byggas upp runt honom. Skolor och flygplatser
	fick hans namn.	Staden Krasnovodsk fick byta namn till Türkmenbasy (som betyder Turkmenernas ledare) efter Nijazov.
	Även en meteorit passade han på att uppkalla efter sig själv.
	Hans namn finns överallt i landet, såväl som hans ansikte. I alla offentliga byggnader i Turkmenistan finns det porträtt av Nijazov.
	Han är ju faktiskt president.
	Det han däremot är mest känd för är alla statyer han lät resa. De flesta föreställande honom själv, men ibland även hans mor.
</p>
<p>
	Den häftigaste statyn av Nijazov är gjord helt i guld
	och placerad högt över gatorna i huvudstaden. Den roterar 360° varje dygn så att Nijazovs ansikte alltid är vänt mot solen.
	Det var uppenbarligen viktigt.
	En annan staty föreställande honom själv placerade Nijazov i Karakumöknen,
	där han även lät bygga en stor konstgjord sjö. Karakumöknen ligger utanför Turkmenistans huvudstad, och det är en av världens varmaste
	öknar. Temperaturen där kan stiga till 50°C. Den platsen valde Nijazov för bygget av ett ispalats.
	Nåt ska väl statens pengar användas till...
</p>
<div class="illustration">
	<img src="http://images.hamsterpaj.net/article_illustrations/typer/guldstaty.png" />
	Den roterande guldstatyn
</div>
<p>
	För att bilda Turkmenistans yngsta invånare till rättskaffens människor gjorde Nijazov även sin insats för skolan. Han förbjöd alla
	böcker som skrivits under sovjettiden, stängde biblioteken på landsbygden (eftersom folk på landsbygden ändå inte kan läsa), och
	såg till att hans egna verk och tal utgör större delen av innehållet i skolböckerna. Skolorna lär ut att älska presidenten och det finns
	i stort sett inga andra böcker än Nijazovs. Ifall det inte räckte skapade han ett nytt alfabet och döpte
	om månaderna och veckodagarna. De fick namn efter honom själv, hans mor, och andra släktingar.
</p>
<p>
	Slutligen såg han även till att underhålla lag och ordning i landet genom att stifta lite lagar som han inte klarat sig utan.
	Balett, opera, och datorspel
	förbjöd han eftersom han inte tyckte det var del av den turkmenska kulturen. Manliga TV-presentatörer förbjöds att ha smink på sig
	för att Nijazov hade svårt att skilja på män och kvinnor. Tobak förbjöds när han själv blev tvungen att sluta röka efter en operation.
	Ytterligare förbjöds saker som långt hår, skägg, och guldtänder. Tänk vad man kan åstadkomma.
</p>
<p>
	Ett av Nijazovs sista lustiga beslut var att alla sjukhus utanför huvudstaden (ca 300 stycken) skulle stängas, eftersom han tyckte det var slöseri att
	ha läkare på landsbygden när de i stället kunde arbeta i staden. Staten måste ju spara pengar.
</p>
<p>
	Vad säger man? Vi har ju ingen roterande Persson-staty i guld, och inte har jag hört nåt övernaturligt om Reinfeldts födsel.
	Jag tror de båda två är rätt okej ändå, och det där med demokrati är ju inte helt fel.
</p>