<p>
Origami kallas konsten att vika papper. Sedelorigami är konsten att vika sedlar, och det är precis vad den här artikeln handlar om. Här visar vi dig hur du viker en stilig skjorta av en vanlig svensk tjugokronorssedel.
</p>

<h2>Du behöver</h2>
<ul>
<li>En tjugokronorssedel, gärna ny och slät</li>
</ul>
</p>


