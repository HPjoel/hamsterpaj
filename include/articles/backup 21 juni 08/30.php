<p>
Idag finns ingen gräns för hur mycket lidande djuret kan få utstå. Djur som utnyttjas i djurförsök får enligt svensk lag utsättas för svår fysisk smärta och annat svårt lidande. I rapporten &#8221;Plågsamma djurförsök&#8221;, utgiven av Djurens Rätt inför Försöksdjurens dag, ges exempel på godkända plågsamma djurförsök. Djur tvångsmatas med höga doser av substanser tills de drabbas av svårt lidande och eventuellt dör. 
</p>

<p>
Andra djurförsök innebär att djur drabbas av allvarliga skador och sjukdomar av genmanipulering, djur hjärnskadas och utsätts för törst och sedan får strömstötar när de försöker dricka, försök avbryts först vid kramper eller allvarliga sjukdomstecken, svåra ledsmärtor framkallas utan att smärtlindring ges, dödsförsök utförs på fiskar, djur görs drogberoende och utsätts för abstinens. I rapporten ges också exempel på försök som genomförts på sådant sätt att apor utsatts för svårt lidande, vilket inte skulle ske enligt den godkända ansökan.
</p>

<p>
Djurens Rätt anser att detta är oacceptabelt därför att de miljoner apor, hundar, möss, råttor och andra djur som utsätts har som människan förmåga att uppleva smärta och annat lidande.  Djurens Rätt kräver att lagstiftningen ändras så att det inte är tillåtet att utsätta djur för obegränsat lidande i djurförsök. 
</p>
   
<p>
Det måste finnas en gräns för hur mycket och hur länge ett djur får lida i ett försök. Alternativ finns. Djur är inte försöksobjekt!
</p>

