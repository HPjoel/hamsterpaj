På Astra Zeneca ska 800 beaglar användas för att testa hur giftiga läkemedel är. Miljoner djur lider i våra laboratorier. För förhoppningen att vi ska bli friskare, vackrare och smartare får djuren betala priset. Detta får uppenbarligen bli hur högt som helst för det enskilda djuret. 
