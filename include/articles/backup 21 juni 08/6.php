<p>
Detta blir en ganska illaluktande rök av förgasad plast. Akta panchisar och astmatiker.
</p>

<h2>Det här behöver du</h2>
<ul>
<li>En eller två pingisbollar</li>
<li>Lite aluminiumfolie</li>
</ul>

<h2>Så här gör du</h2>
<ol>
<li>Klipp pingisbollen/bollarna i bitar, cirka 1x1cm</li>
<li>Lägg bitarna i en liten hög på en bit aluminiumfolie</li>
<li>Riv loss en lite större bit aluminiumfolie</li>
<li>Tänd eld på pingisbollsbitarna och vänta någon sekund</li>
<li>Kväv elden med den andra biten aluminiumfolie. Lägg den ovanpå pingisbollarna och tryck till så att det slutar brinna</li>
</ol>

