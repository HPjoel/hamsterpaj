<h2>Presskonferens på The Marriot Hotel</h2>
<p>
	Kvällen efter galapremiären frågar våra brittiska guider om de kan få frågorna som vi har förberett. Jag och Emma tittar på varandra
	- vadå förberett frågor? På lite stapplande engelska förklarar jag att de kan få frågorna imorgon, innan vi åker till London Eye.
	Det går bra, situationen är tillfälligt räddad - nu måste vi bara hafsa ihop lite frågor för att inte göra bort oss.
</p>

<p>
	På morgonen åker vi taxi genom London-trafiken, det är bilar överallt, man gör U-svängar och backar på de mest underliga ställen,
	dessutom kör alla bilar på fel sida vägen. Puh!<br />
	Framme vid hotellet tar personalen vänligt men bestämt ifrån Emma hennes väska och bär den upp för ett par meter trappa, det här
	är inget billigt hotell förstår vi. Efter att ha irrat runt i korridorer där tiden stått stilla sedan förra sekelskiftet leds vi in i ett
	rum där presskonferensen ska hållas. Kameror är förbjudna.<br />
	Efter en stund kommer stjärnorna in och sätter sig:
	<ul>
		<li>Regissören, Tim Story</li>
		<li>Reed Richards (Ioan Gruffud)</li>
		<li>Sue Storm (Jessica Alba)</li>
		<li>Johnny Storm (Chris Evans)</li>
		<li>Ben Grimm (Michael Chiklis)</li>
	</ul>
</p>

<p>
	Journalisterna börjar fråga och jag klottrar som en dåre i min anteckningsbok, på nåt sätt ska jag ju svänga ihop en artikel efteråt.
	Det som slår mig ganska snart är att stjärnorna inte är så särskilt märkvärdiga, helt vanliga trevliga människor, som förvisso 
	gått lite mediaträning. Men ändå, de verkar ändå inte så himla märkvärdiga, de är människor som är duktiga på sitt jobb.
</p>

<p>
	Ioan Gruffud som spelar Reed Richards får frågan vad som var svårast eller jobbigast med filmen. Både för skådespelaren och
	karaktären var det dans-scenen där han dansar med två lättklädda tjejer. Han behövde ett par glas champagne innan han vågade köra
	den scenen. Dessutom var han tvungen att göra om den flera gånger ensam för att specialeffekt-killarna skulle kunna göra sitt jobb.
</p>

<p>
	Någon frågar Jessica Alba om hon känner press på sig att vara supersmal. Alba svarar lite lätt förolämpat att hon inte är någon
	"size zero" och att hon inte tror sig ha varit det nån gång heller. Hon tränar för att hon mår bra av det och för att hålla sig
	frisk och kunna leva ett bra liv. Om man inte tar hand om sig själv kan man ju drabbas av hjärtinfarkt, diabetes och andra otäckheter!
</p>

<p>
	Frågan om träning går laget runt och när den når Michael som spelar Ben (sten-killen) svarar han:<br />
	<em>&ndash; Well you other guys got a six pack. I've actually got an eight-pack... In my fridge!</em><br />
	<br />
	Vi börjar prata om hur det är att vara känd och Chris Evans (spelar Johnny, eld-killen) berättar att han såg en liten unge leka
	med en figur av honom själv på Halloween i höstas.<br />
	<strong>Michael berättar om en gång för några veckor sedan:</strong><br />
	<em>&ndash; Well, I was taking a walk when I heard my own voice from behind saying "It's clubberin time". I turned around and saw a
	little kid playing with theese giant gloves, when he hit 'em together they played my voice. I looked at him and he looked at me, I
	couldn't tell who was the most excited.</em>
</p>

<p>
	Mot slutet berättar Jessica att hon aldrig varit naken i en film, nakenscenerna i filmen är inspelade med en hudfärgad dräkt. Sedan har
	specialeffektskillarna redigerat så att det ser verkligt ut.<br />
	Alla skulle vilja kunna flyga om de fick välja en superkraft, förutom Ioan (Reed) som skulle vilja kunna läsa alla böcker som någonsin skrivits,
	på alla språk.
</p>

<h2>London Eye med regissören Tim Story</h2>
<p>
	Sista aktiviteten i London var ett varv i det gigantiska parisehjulet London Eye tillsammans med regissören Tim Story.
</p>

<p>
	<strong>&ndash; What differs from the first Fantastic Four movie and this one?</strong><br />
	<em>&ndash; It's more action in the follow-up. We were able to take the super heroes on a world wide trip, visisting both the New York
	and London. We also got the possibilities to do whatever we wanted, like tipping the London Eye over and emptieing Thmessen.<br />
	 It's cooler, more action and much more intense, I had a hard time squeezing it all down to 95 minutes.</em><br />
	 <strong>&ndash; If you could go back in time and alter the first movie, would you change anything?</strong><br />
	 <em>&ndash; No, I don't think so. The movie did what it was supposed to do, introduce the characters. Mabye I'd add some more action.</em>
</p>

<p>
	Tim berättar om gästfiguren <em>the Silver Surfer</em> som dök upp på bioduken för första gången i den här filmen, 
	men som antagligen kommer att dyka upp i en egen film sen. En film som Tim inte vill regissera, han är nöjd med att
	ha fått introducera silversurfaren.<br />
	Vi undrar (eller rättare sagt, de insatta filmjournalisterna undrar och jag försöker se ut som om jag hänger med) om de tittade på
	roboten T1000 från <em>Transformers</em> när de skapade Silversurfaren.<br />
	<em>&ndash; Jo, visst tittade vi på den, vi var mest intresserade av hur dom löste det rent tekniskt.</em>
</p>

<p>
	<strong>&ndash; Did you ever feel sorry for the characters? Where you ever tempted to relieve Ben from his stone skin or punishing Johnny 
	for being so unbearable?</strong><br />
	<em>&ndash; Haha, no! I like throwing the actors and the characters into problems! It creates tension and makes them feel
	uncomfortabel, making the movie more exciting to watch!</em>
</p>

<p>
	När jag avslutningsvis frågar om specialeffekterna - fick de göra allting de ville och kunde de åstakomma alla effekter - svarar Tim att
	det bara handlar om tid och pengar. Man kan göra allting om man bara vill.<br />
	Det krångliga är oftast att på ett smidigt sätt försätta karaktärerna i alla situationer utan att det känns konstigt eller framstressat.
</p>

