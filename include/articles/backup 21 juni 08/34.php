<h2>Förhandsvisning</h2>
<p>
	För det första höll vi på att komma försent till Soho Square, där lokalen skulle ligga. För det andra hade jag lyckats
	glömma info-pappret på hotellrummet, det var nära att vi inte hittade dit. Men sen såg vi ingången till Twentieth Century Fox och gick in.
	Efter att ha velat fram och tillbaks genom säkerhetskontrollen, ena gången skulle vi ha med oss kameror - andra gången inte, så kom vi till
	slut in i en liten biosalong. Hur filmen var kan du läsa i <a href="/texter/?article=35">recissionen av Fantastic Four: Rise of the Silver Surfer</a>.
</p>

