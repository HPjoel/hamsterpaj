Heliumballonger på ryggen, bara så många att man precis svävar uppåt. Och sedan en sån där sele som man har när man klättrar, med linan fast i marken.  Då skulle man kunna fira upp sig själv, långsamt, långsamt upp i luften. Kanske 50 meter eller så, älvsborgsbron är ju bara 43 meter. Visst skulle det vara häftigt?
