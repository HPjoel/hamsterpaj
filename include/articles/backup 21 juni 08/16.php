<h2>Vad behöver man rent praktiskt då?</h2>
<p>
Jag funderade lite på det. Förutom webbkameror, 3G-telefoner, säkerhetssele, linor, komradio, stillbildskamera och lunchkorg behövs det.
<ul>
<li><h3>Helium</h3>
Helium har vid normalt lufttryck en densitet på 0,1785kg/m<sup>3</sup>.<br />
Luft har densiteten 1,2kg/m<sup>3</sup>.<br />
Helium väger alltså ungefär 0,2 kilo per kubikmeter och luft väger 1,2 kilo per kubikmeter. Det betyder att en kubikmeter helium kan lyfta ungefär ett kilo.<br />
Eftersom jag väger runt 75 kilo kommer det behövas minst 80 kubikmeter helium för att lyfta mig och all utrustning. Om man tänker sig att ditt rum är 2,5m högt, fyra meter långt och fyra meter brett så skulle man behöva fylla ditt rum två gånger om med helium.<br />
Så mycket helium skulle kosta ungefär 20 000kr om man köper från <a href="http://www.ballonger.nu/index.php?page=helium_start" target="_blank">www.ballonger.nu</a>. Fast det blir säkert billigare om man köper direkt från AGA. 
</li>

<li><h3>Ballonger</h3>
Jag bad <a href="/traffa/profile.php?id=57100">Ace</a> på Hamsterpaj att se hur mycket vatten det rymms i en vanlig ballong, cirka fem liter. Eftersom en kubikmeter är tusen liter så skulle det behövas ungefär 80 000 liter helium, vilket blir 16 000 vanliga ballonger.<br />
Jag gissar på att man kan blåsa upp och knyta en ballong på ungefär fem sekunder om man använder en tryckbehållare. Det innebär tolv ballonger i minuten, 720st i timmen för en person. Är man tio personer så borde man kunna blåsa upp alla 16 000 ballonger på strax över två timmar.<br />
Jag tror jag får komma på en annan lösning, typ köpa större ballonger...
</li>
</ul>

