<h2>Bakgrund</h2>
<p>
Jag läste för ett tag sedan om en kille i USA som hade fäst fyrtiofem väderballonger på en trädgårdsstol, fyllt dom med helium och seglat iväg upp i skyn. Med sig upp hade han en kamera, lite ballast, en kom-radio, en höjdmätare och en luftpistol, för att kunna skjuta hål på ett par ballonger när han ville ner.<br />
Larry, som killen hette susade iväg nästan fem kilometer upp i luften och två förbipasserande flygplan hos TWA Airlines rapporterade vad de såg till kontrolltornet. Han blev yr av syrebristen och sköt ett par ballonger för att falla neråt. Från början tänkte han landa på en gräsplätt, men missade och fastnade med ballongerna i ett par högspäningsledningar. Brandkåren kom och räddade Larry och där slutade hans äventyr.<br />
Källa: <a href="http://www.snopes.com/travel/airline/walters.asp" target="_blank">www.snopes.com</a>.
</p>

<h2>Min idé</h2>
<p>
