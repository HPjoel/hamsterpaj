<p>
	Såhär brukar ackord se ut när de förklaras i musikböcker. Om du förstår hur man ska läsa dem kan du lära dig massa nya ackord.
	De lodräta linjerna representerar strängarna. Tänk dig att man ställt gitarren på golvet med halsen pekandes rakt upp.
	De vågräta linjerna representerar bandstavarna på gitarrens hals. Varje liten "ruta" som formas av
	två bandstavar och halsens kant brukar man kalla för ett band. Beroende på vilket band man håller fingret får strängen en annan ton.
	Där det är markerat med en svart prick
	är där du ska hålla fingret. Prickarna är alltså ritade på strängarna och på ett särskilt band, vilket avgör tonen.
</p>
<p>
	Längst upp ser du vita prickar (eller ringar) och kryss. Ett kryss betyder att du inte ska spela på den strängen under krysset. En ring betyder
	att du spelar på strängen men inte håller något finger på den. Siffrorna nederst talar om vilket band du ska trycka ner strängen på.
</p>

<p>
	Så småningom kommer att komma det fler tips på enkla låtar, förklaringar till hur man läser tabbar och lär
	sig nya ackord, hur man stämmer sin gitarr utan stämmare, hur man byter strängar, osv.
</p>