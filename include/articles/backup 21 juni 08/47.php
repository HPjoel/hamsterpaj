<h3>Kärleken Väntar</h3>
<p>
	Nästa låt är ännu lättare. För att spela Kärleken Väntar av Kent så räcker det nästan med att kunna spela Am och F.
	Till refrängen behöver du också kunna C och Em. (Ringfingret trycker inte ner någon sträng när du spelar Em, tyvärr blev bilden inte helt bra.)
</p>

