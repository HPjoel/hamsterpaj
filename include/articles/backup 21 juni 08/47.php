<p>
	Ett F-ackord tar man oftast som på bilden till vänster nedan men det är svårt när man är nybörjare. Som tur var kan man spela ett
	förenklat F-ackord. Då håller man fingrarna som på bilden till höger, och spelar bara på de fyra nedersta strängarna. Lägg märke
	till att pekfingret ska trycka ner båda de tunnaste strängarna.
</p>

