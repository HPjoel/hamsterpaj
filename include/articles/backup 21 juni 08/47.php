<h2>Har du en gitarr liggandes på vinden? Såhär kommer du igång med att spela</h2>

<p>
	Gitarr är kul och inte alltför svårt att spela, det gäller bara att lära sig grunderna.
	Först ska man se till att man har en gitarr. Det finns olika 	typer av gitarrer och strängar
	men det behöver vi inte gå in på än. Se bara till att du har en gitarr och fixa nån/nåt som
	kan stämma den åt dig.
</p>
