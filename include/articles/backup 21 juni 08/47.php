<h3>Stämma gitarren?</h3>
<p>
	För att stämma din gitarr behöver du någon slags stämmare. Man kan använda stämningsapparater,
	stämpipor, och stämgafflar. Den som är lättast att använda är stämningsapparaten, det gäller
	bara att förstå hur den funkar. Det enklaste är självklart om du känner nån som kan stämma gitarren
	åt dig, men gör du inte det får du lite hjälp här. Vill du inte slösa dina pengar på en
	stämningsapparat kan du ladda ner en från internet. Den här är bra och gratis att ladda ner:
<br />http://home.att.net/~jorgeguillen/minituner.html
</p>
<p>
	Att stämma gitarren är sen ganska lätt. Slå på tjockaste strängen för att höra hur den låter.
	Stämningsapparaten visar om du får rätt ton, som i detta fallet ska vara ett E. Har strängen inte rätt
	ton får du vrida på den "skruv" som strängen sitter i. Den reglerar hur hårt strängen är spänd och
	ändrar tonen.
	Det borde som sagt synas ett E och lite streck i olika färger på skärmen när du använder stämmaren.
	Ser du en annan bokstav får du skruva och spela på strängen tills du får ett E. Strecken med de olika
	färgerna visar hur pass nära tonen du är. För att få en perfekt ton ska det bara synas grönt och strecken
	ska vara så nära mitten som möjligt.
</p>
<p>	
	Stämmaren hjälper dig också att veta om tonen är för hög eller för låg genom att peka åt vilket håll du
	borde gå, upp eller ner. Skruvar du strängen åt ena hållet spänns den mer och blir högre, skruvar du åt
	andra hållet blir den lösare och du får en lägre ton. Man märker rätt snabbt vilket håll man ska skruva åt
	för att få rätt ton.	
	Har du väl stämt översta strängen är det bara att fortsätta. Gör samma sak med resten av strängarna. Enda
	skillnaden är att du ska få andra toner på strängarna. Börjar du på den tjockaste och går mot den smalaste så borde strängarna ha
	följande toner: E, A, D, G, B, E.
</p>
