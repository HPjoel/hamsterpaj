<h3>Fler ackord</h3>
<p>
	Det här är ett lätt sätt att börja spela gitarr. Man kan alltid göra låtarna mer avancerade, men man kan också
	alltid förenkla dem. När man väl har lärt sig lite ackord eller lärt sig hur man själv kan ta reda på nya ackord så är det
	lätt att leta upp nya låtar att spela. Det brukar finnas hemsidor för sånt. En hemsida där du kan lära dig många nya ackord
	är www.musikakuten.se.
</p>


