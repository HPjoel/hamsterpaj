<h3>Dags att spela</h3>
<p>
	Ackord brukar man få lära sig i skolan vad det är. Kan man några ackord så räcker det för att spela en låt.
	(Ifall du tycker bilderna är otydliga så finns alla ackord från den här artikeln samlade i slutet med tydliga beskrivningar.)
	Här visar vi hur du enkelt kan spela Knock Knock Knockin' on Heaven's Door. Börja med att lära dig ett G- och ett D-ackord:

