<p>
	Nu kan du prova att växla mellan alla ackorden du lärt dig. Spela först G, sen D, sen Am7, G, D, C, G, D, Am7, osv.
	Sen är det bara att spela låten. Spela två takter på varje ackord. Precis som man kan klappa i takt kan du tänka dig att
	du slår på strängarna varje gång du annars skulle klappat. Kollar du på texten och ackordföljden här nedan så ser du att
	du får spela sista ackordet på varje rad två gånger, det vill säga fyra takter.
	
	tips - Är det svårt att hinna med och byta ackord? Prova att bara spela vartannat ackord och vara tyst två takter imellan.
	Du kan också prova att sjunga med, det gör det hela roligare. Du kan öva själv innan du försöker spela med till låten.
	<br />(På refrängen
	börjar du sjunga andra gången du slår G-ackordet, på andra takten vill säga.)
</p>
	
