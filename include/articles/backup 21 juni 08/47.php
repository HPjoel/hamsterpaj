<p>
	För att kunna hålla ett så kallat barréackord måste man kunna trycka ner fler strängar samtidigt genom att lägga pekfingret över alla strängar som ska tryckas ner. Om du ska ta F-ackordet till vänster måste pekfingret allå ligga över alla strängarna och trycka ner dem så pass mycket att det inte låter konstigt.
</p>
<p>
När du kan ackorden är det bara att spela. Byt ackord när du kommer till den del av texten som står precis under ackordet.
</p>
