<p>
<strong>Tips!</strong> - G kan vara ett svårt ackord om man är nybörjare. Om du istället bara spelar på de fyra tunnaste strängarna räcker det med att
hålla ringfingret på nedersta strängen.
</p>

<p>
	Har du väl fått koll på hur du ska hålla fingrarna är det bara att använda andra handen till
	att slå på strängarna. Man kan dra med tummen eller pekfingret eller både och över strängarna.
	Det finns de som tycker att man ska hålla ihop tummen och pekfingret och använda tippen av båda
	för att spela. Man väljer själv hur man vill göra.	
	Börja med att hålla G-ackordet och spela. Håll sedan D-ackordet
	och spela bara på de fyra tunnaste strängarna. Växla fram och tillbaka mellan G- och D-ackordet några gånger.
</p>
<p>
	Nästa ackord är C och Am7:
</p>
