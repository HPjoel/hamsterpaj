<p class="ingress">
IRC, Internet Relay Chat är ett av de äldsta och mest använda sätten att chatta på över Internet. Vi på Hamsterpaj kör våran chatt som IRC och många CS-spelare brukar använda sig av Quakenet som är ett stort IRC-nätverk.<br />
I den här guiden kommer vi att visa hur du installerar och kommer igång med mIRC, det mest populära programmet för att chatta på IRC.
</p>

<h2>Ladda hem och installera mIRC</h2>
<ol>
<li>Besök <a href="http://www.mirc.com/" target="_blank">www.mirc.com</a></li>
<li>Välj "Download mIRC" i menyn till vänster</li>
<li>Holland är ett land med snabb uppkoppling, välj Holland i listan och spara filen på skrivbordet</li>
<li>Starta installationen och klicka dig igenom den, du behöver inte fundera över inställningarna utan kan bara trycka "Next" hela tiden</li>
</ol>

<h2>Koppla upp dig mot IRC-servrar</h2>
<p>
Första gången du startar mIRC visas en inställningssida.
<ul>
<li><h3>Full Name</h3>
Kort information om dig själv, denna infon syns när någon gör en /whois på dig. Själv brukar jag skriva ungefär <em>"18 year old web developer from Sweden, working with LAMP/XTHML/CSS"</em>.
</li>
<li><h3>Email Address</h3>
Används inte längre, skriv vad du vill här
</li>
<li><h3>Nickname</h3>
Det du vill heta på chatten
</li>
<li><h3>Alternative Nickname</h3>
Om någon redan har namnet du vill ha så används detta istället
</li>
Välj <strong>[OK]</strong> för att starta mIRC.
</ul>


