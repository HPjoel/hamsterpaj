<strong>Ni har säkert lagt märke till dem, de där bjässarna som rullar, mäktigt stora, bland andra fordon i asfaltsdjungeln. Det är i alla fall ett gäng som upptäckt dem, och de tycker inte om dem. Asfaltsdjungelns indianer är gänget som fått nog av stadsjeepar och nu vill få bort dem från städernas gator.</strong>
</p>
<p>
	De stora SUV:arna, eller stadsjeeparna som de också kallas, är större än vanliga bilar, har kraftigare motorer och är ofta fyrhjulsdrivna. Egentligen är ju jeepar till för att köras i terräng, då en stark motor och större storlek gör det lättare att ta sig fram, men nu finns jeeparna alltså även i stadsversion. Deras storlek gör att de känns säkrare att köra i och ger ägaren mer utrymme i bilen. Tyvärr har detta dock ett pris, eftersom SUV:arna är större drar de mycket mer bensin än vanliga bilar, och det ökar koldioxidutsläppen. Med andra ord är det som med alla andra moderna leksaker; vi vill ha lite mer lyx, lite större storlek, och får det på bekostnad av miljön.
