<h3><strong>Informera eller genomföra aktioner?</strong></h3>
<p>
	Indianernas argument är att stadsjeeparna skadar miljön. De påpekar också att SUV:arna är onödiga lyxprylar som främst ägs av rikare män(niskor), människor som egentligen har råd att köpa miljövänliga bilar. De kallar stadsjeeparna för penisförlängare som ger ägaren en känsla av höjt självförtroende och status. SUV-ägarna tycker att indianerna har gått helt fel till väga, och att det är dumt att ge sig på enskilda personer. De tycker att det finns bättre sätt att starta diskussion. Många tycker att det är bättre att bara informera, utan att tömma några däck, att det räcker att dela ut breven eller flygblad.
</p>
<p>
	Indianerna verkar dock ha en poäng eftersom det är många som köper SUV-ar trots att de vet om att de skadar miljön. Däremot kan man fråga sig om deras hyss inte får motsatt effekt och bara eggar SUV-ägare att köra ännu mer med sina bilar. Dessutom finns det de som faktiskt behöver sina stadsjeepar. Bland ägarna som hört av sig så finns det handikappade som behöver utrymmet i en stor bil och en läkare som inte hunnit i tid till en operation för att hans däck tömdes. Även en ägare av en Volvo XC70 har fått sina däck tömda och det verkar alltså som att asfaltsdjungelns indianer gjort en miss. Deras svar på detta är att de försöker undvika att lufta bilar som uppenbarligen används i terräng, till arbete, eller som har handikappstilstånd. Och att det finns lokaltrafik för den som vill ta sig nånstans.
</p>

<p>
	En sak är säker, indianernas handlande har väckt uppmärksamhet. Det viktiga är nu att skapa debatt ifall det ska leda nånstans. För att göra någon skillnad kan det faktiskt krävas lagstiftning eller att en organiserad, seriösare diskussion (sånt där som vuxna gillar) förs mellan SUV-ägare och Asfaltsdjungelns indianer. Hamsterpaj ger i vilket fall tummen upp till alla initiativ att jobba mot klimatförändringen och tycker om indianernas busstreck som lyckats uppmärksamma och skapa diskussion runt ett viktigt ämne.
