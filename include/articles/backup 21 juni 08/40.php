<p>
	På tisdag släpps en ny singel i äkta sommarstuk med tuggummitoner
	och trallvänlighet. Den heter &#8221;Summertime&#8221; och är skapad av
	Sandra Gee och hennes manager Fredrik Dandy. Det första jag
	tänker på när jag hör den är stora blå bubblor,
	Gunther & the Sunshine Girls, och högstadiedisko. Det är en typisk
	tuggummi-pop låt som studsar fram och tillbaka och kletar sig fast
	i hjärnan. Räkna med att höra den på stranden såväl som på stan i sommar.
</p>

<p>
	För att ta reda på vem den nya sångerskan Sandra Gee är beger jag mig
	till Café Vallgatan för att träffa henne och hennes manager. Det är
	en glad och sprallig tjej i rosa keps jag möter och vi slår oss
	ner för att prata. Kvällen innan var hon i Stockholm och uppträdde
	men trots att hon är trött är hon pratsam och intervjun rullar på bra.
</p>

