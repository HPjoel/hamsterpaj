<p>
Ett papperflygplan av ett vanligt A4, fast denna modell har en vass spets. 
</p>

<h2>Du behöver</h2>
<ul>
<li>Ett helt vanligt A4-papper, men gärna lite grövre papperstjocklek</li>
</ul>
</p>


<h2>Så här gör du</h2>
<ol>
<li>Vik pappret på mitten.</li>

<li>Vik in övre hörnen av papperet mot mitten.</li>

<li>Vik ner spetsen mot de invikta hörnen, så att du får en platt nos.</li>

<li>Vänd på papperet för att börja vika vingarna, vik in papperet mot mitten så att du sparar ca 2 cm till mitten vikningen. Gör samma sak med andra sidan. </li>

<li>Vik upp papperet igen och vik längs mittenvikningen, du ska nu ha en liten flygplanskropp och två stora vingar.</li>

<li>Vik upp yttre kanten på vingarna ca 1 cm på båda sidor</li>

<li>Nu är planet klart för sin första provflygning!</li>

</ol>

