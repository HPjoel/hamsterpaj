<strong>
Sugen på större TV men trångt där hemma? Lösningen heter Vuzix Iwear VR920! Det är ett par virtuella glasögon som projicerar en 62” bildyta 3m framför ögonen.  Cirka 2500kr kostar det om man vill ha ett par.
</strong>

<p>
Vuzix heter företaget som står bakom Iwear VR920. Ett par glasögon som inte bara visar en bildskärm utan kan även användas till att spela  datorspel i 3d-miljö. Det finns två olika typer av spelupplevelser att tillgå med hjälp av Iwear VR920. Dels de som fungerar helt vanligt och ser ut som på vilken skärm som helst, dels de som även stödjer 3d-världen och styrs med hjälp av huvudrörelser.  En lista över vilka titlar som stöds av de olika lägena finns på tillverkarens hemsida.
</p>

<p>
Ett par virtuella glasögon är perfekt att ha med sig, inte bara under själva resan men även framme i sommarstugan, på hotellrummet eller var man nu kan tänka sig vilja se på TV, eller spela spel.
</p>

<p>
I dagsläget stödjer inte alla operativsystem huvudrörelser men med tanke på dagens utveckling lär det bara vara en tidsfråga.  Det kan mycket väl bli så inom en snar framtid att alla spel släpps anpassad för att användas av ett par virtuella glajjor.
</p>