<p>
Djuret har likheter med både hund, sköldpadda och dinosaurie. Att det därför skulle handla om en mutation är inte helt omöjligt menar vissa forskare. Andra påstår att det endast är ett PR-trick, att det påstådda djuret är en fejk.  Mats Höggren, som är zoologisk chef på Kolmårdens djurpark, hävdar bestämt att det rör sig om bluff och båg. Ytterligare en förklaring skulle kunna vara att monstret kommer från ett djurförsökscenter nära Long Island.
</p>
