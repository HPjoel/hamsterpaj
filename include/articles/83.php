<p>
Varelsen hittades på en strand i Montauk i mitten av juli och nyheten har spridit sig som en löpeld över hela USA.  Överallt talas det om att en sensation har inträffat. På olika bloggar runt om i världen spekuleras det om vad det kan tänkas vara för något strandfynd man funnit. Skulle det visa sig vara en ny djurart är det den första att upptäckas på väldigt lång tid i den storleksklassen. Nu för tiden upptäcks sällan annat än olika arter av småkryp. 
</p>
<p>
Vad det än rör sig om är dock de flesta enade om att det inte är någon vacker skapelse i alla fall. Monster är nu skickat för närmre analys.
</p>