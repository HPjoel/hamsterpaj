<strong>
I USA har man funnit ett monster som man förmodar kommer någonstans ifrån yttre rymden.  Hur det tog sig genom atmosfären utan att brinna upp är ännu ett mysterium. Är det kanske därför varelsen saknar både päls och skal?
</strong>
