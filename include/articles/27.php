<p>
Nu i och med sidans stilbyte så kan det ju tänkas att en del av er användare känner att ni inte riktigt passar in, men var lugna, här kommer Emolands kompletta guide till att bli en emokid.
</p>

<p>
Först av allt, utseendet är A och O! Är man emo måste man se ut på ett speciellt sätt. Håret är kanske det allra viktigaste. Det ska vara korpsvart och ganska långt. Man bör absolut kunna täcka ena ögat med den sk &#8221;snedluggen&#8221;. Skulle man av någon anledning le någon gång så får ju det synas. Svart färg finns att köpa billigt i närmsta livsmedelsbutik. Det gör inget om det blir en  utväxt efter ett tag, det går att färga om.
</p>

<p>
Vidare är sminkningen väldigt viktig. För att få en bra grund till ett emoface är det av största nödvändighet att inte vistas utomhus, speciellt inte i solen!
För att matcha det kritvita ansiktet krävs rejäla mängder kajal runt ögonen, kombinera gärna med lite röd ögonskugga för att få fram ett rejält djup
En piercing i ögonbrynet eller läppen är en snygg accessoar.
</p>

<p>
Till utseendet hör även klädseln. Längst upp, runt halsen, ska man ha en palestinasjal. Färg är ganska valfritt men svart är alltid ett säkert kort. På överkroppen är det randigt som gäller, en urtvättad grå-svart-randig t-shirt från H&M fungerar alltid. Undertill är det taaaaaajta Cheap Monday-jeans man ska ha! Stentvättade, otvättade eller skotskrutiga spelar ingen roll, så länge det är tight, stuprör och lite häng så är man helt rätt ute. Vad gäller skor finns det egentligen inga andra alternativ än Converse All-Star. Färg är upp till var och en men det är viktigt att de INTE ser ut som nya! Har man inga trashade kan man gå till någon secondhand-affär som Myrorna och hitta ett par. Ju trasigare desto bättre.
</p>

<p>
Alla emos lyssnar på musik, hög volym är självklart, så att alla andra också kan höra. Är man lite feg kan man börja med Broder Daniel och Bright Eyes, det är bra musik att mjuka upp handlederna till. När man känner att man är mogen att gå vidare till lite känslosammare band så kan bra alternativ vara The Used och Kent (men se upp för skivan Vapen och ammunition! Alldeles för poppig och vanlig).
</p>

<p>
Även om man som emokid bör stanna inomhus så finns det några samlingsplatser som det är okej att vistas på. Plattan i Stockholm är kanske det mesta kända stället. Där sitter man i stora trappan vid ingången till T-centralen och kindpussar varandra. Barnsligt beteende är vanligt för att väcka uppmärksamhet vilket man som emo eftersträvar att få hela tiden.
</p>

<p>
Som emo har man en favoritsyssla, nämligen att karva sig i armarna. Det är något alla gör och den med flest och störst ärr vinner. Satsa på översidan av underarmen, chansen att någon ser snitten är mycket större då. Använd inte pappas rakhyvlar, de är antagligen för slöa. Leta istället upp de där större, bredare utan handtag. Finns i närmsta livsmedelsbutik.
</p>

<p>
Att bli emo är kanske inte helt lätt till en början men efter ett tag klarnar det nog och du blir precis lika mycket emo som alla andra på Emoland!
</p>