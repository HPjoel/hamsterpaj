<h2>VARNING! Detta experiment kan vara farligt, läs denna texten först!</h2>
<p>
Väteperoxid är ett kraftigt oxiderande ämne, det kan missfärga kläder, förstöra dyra golv, ge dig hudproblem och skada din syn. Var därför väldigt försiktig när du använder det.
<ul>
<li>Ta inte mer än en tesked av varje ingrediens</li>
<li>Akta ögonen, luta dig bort från ämnena när du blandar dom (vi skojar inte)</li>
<li>Ämnena reagerar omedelbart med ett fräsande och blir kokheta. Det kan komma en del stänk, så ha oömma kläder</li> 
</ul>
Panchisar, småbarn och personer med luftrörsproblem, typ astmatiker, ska inte utsättas för detta "practical joke".
</p>
</div>

<p>
Den här blandningen bildar ett gasmoln med extremt illaluktande gas, stanken kan beskrivas som "äggfis" eller ruttna ägg. Lukten går ganska enkelt att vädra ut, men testa helst det här utomhus.
</p>

<h2>Det här behöver du</h2>
<ul>
<li>Väteperoxid, teknisk (15%-20%)<br />
Säljs i färghandeln och brukar kosta mellan 60-80kr. En vätska som ser ut och luktar som vatten (smaka inte). Finns inte i alla färgaffärer, utan man kan behöva åka runt och fråga. Står ibland bakom disk, så man behöver prata med personalen.
</li>
<li>Remol<br />
Ett vitt och lätt illaluktande pulver, säljs i färghandeln och stora varuhus, typ Coop Forum och ICA Maxi. Remol är ett blekmedel och brukar stå bland rengöring/tvättmedel. Kostar runt 50kr för en liten flaska.</li>
</ul>

<h2>Så här gör du</h2>
<ol>
<li>Häll upp lite mindre än en tesked Remol i ett glas eller en mugg som du inte är rädd om.</li>
<li>Ta ungefär en tesked Väteperoxid (inte mormors silversked eller någon som dina föräldrar är rädd om).</li>
<li>Luta dig bakåt och häll peroxiden på Remolen. Reaktionen är kraftigare än man tror, ett högt *poff* hörs och ett gasmoln bildas.</li>
</ol>
Tänk på att väteperoxid reagerar med metall. Johan fick slipa sin diskbänk med våtslippapper för att få bort märkena (hoppas inte hyresvärden märker nåt).

