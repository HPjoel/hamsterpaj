<h2>Om mig</h2>
<p>Hej.
Jag heter Hannele men är mer känd som Shedevil. Är ordningsvakt 
Och sexvetare på hamsterpaj.</p>
<br>
<p>Jag är en glad och positiv tjej.
På fritiden umgås jag med mina kompisar eller min pojkvän och jag har egen häst, men rider inte längre.
Är diktskrivare och har gett ut en egen diktbok.
Jag sjunger i en feministisk kör men det betyder inte att jag avskyr män och jag anser att alla har rätt till sin åsikt, även om jag inte håller med om den.</p>
<br>
<p>Till hösten(08) kommer jag påbörja min utbildning som kurator. Och när den är färdig kommer jag fortsätta att utbilda mig till sexolog.
Min högsta dröm är att jobba på en ungdomsmottagning.</p>
