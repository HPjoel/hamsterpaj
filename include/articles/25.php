<p class="ingress">
Onsdagen den tjugiförste februari 2007 åkte jag, Johan från Hamsterpaj, upp till Stockholm för att träffa Hans Peter Baxxter, frontfiguren i det tyska technobandet Scooter. Efter att ha virrat runt i krokarna runt Nybroplan hittade jag och ekonomi-Pär slutligen Berns Hotel, där killen bodde.
</p>

<h2>Vem är HP Baxxter, och vilka är Scooter?</h2>
<p>
Scooter startade i början av nittiotalet och har haft hits med <em>"Maria, I like it loud"</em>,
<em>"Faster, harder, Scooter"</em>, <em>"Ramp! (the logical song)"</em> och <em>"Hypa hypa"</em>.
De har kännetecknats av pitchade kvinnoröster, Baxxters "rappande" och ett ganska "avskalat" eurodisco-sound.
</p>

<img src="http://images.hamsterpaj.net/article_illustrations/scooter.jpg" />

<p>
Ikväll träffar vi Baxxter i en stor svit på Berns hotell. Vi blir insläppta av en PR-kvinna och hälsar på stjärnan. Han är brunbränd med kritvitt hår och välklädd, en snygg skjorta och ett par stora dödskalleringar. Vi slår oss ner i var sin fåtölj och börjar intervjun. Han bryter lätt på tyska och vi bryter på göterborska.
</p>

<p>
<strong>Why did you start Scooter?</strong><br />
<em>- We started out as a hobby remix band back in the early ninetees, it was just me and some friends experimenting with some music. We had no thought about making hit music.</em><br />
<strong>You've recently released a new album called "The Ultimate Audial Orgasm", will it differ from the well-known Scooter sound?</strong><br />
<em>- Well, you will recognise the style, it's defenitively Scooter, but you may experience a little bit more "electro house". I don't know how to explain it, it's different but the same.</em><br />
</p>

<p>
Vi har fått träffa Baxxter eftersom Scooter just nu lanserar sitt nya album, det första som har hänt
på länge. När jag frågar varför vi inte hört så mycket från Scooter de senaste åren berättar han att
Scooter går olika bra i olika länder. Det har alltid varit populära i Tyskland medans intresset i
Sverige svänger fram och tillbaka.<br />
Klart är i alla fall att det går bra för det tyska electrobandet, de planerar just nu sin största turné någonsin.
</p>

<p>
<strong>Many of our users wonder when you will come to Sweden and play</strong><br />
<em>- Some time during 2008 I believe, our manager has just started to plan the tour. This will be the first "real" tour where we bring our own PA system and travel between arenas. We've only made some single concerts and played at festivals before. This will be much more serious and better.<br />
I don't really know when we'll get to Sweden, but I promise you we will.
</em>
</p>

<p>
Scooter har alltid förbryllat mig med sina konstiga texter och utrop, i slutet av låten Weekend ropar Baxxter till exempel <em>"Respect to the man in the ice cream van"</em>. Nu när jag har möjligheten måste jag ju bara fråga.
</p>

<p>
<strong>What about the man in the ice cream van?</strong><br />
<em>- Hahaha, that's an old thing! We've always been a great fan of the techno group KLF, and that shout is like a tribute to them. During christmas eve, one winter in the early nintees they drove an ice cream van thru the city of London, giving out free beer to the homeless.</em>
</p>

<p>
Baxxter berättar vidare om KLF, som gjorde många galna saker under sin karriär. En gång brännde dom KLF miljoner brittiska pund på ett stort bål, framför pressens kameror. Lite KLF bjöd dom in pressen till en presskonferens, och när konferensen skulle börja släppte dom in livs levande får bland journalisterna.<br />
När Baxxter skriker "How much is the fish" i slutet på en låt gör han det som en hälsning till KLF, en av deras låtar hette just "How much is the fish".<br />
Vi pratar vidare kring hur Scooters låtar kommer till och hur dom jobbar. Vanligtvis börjar killarna i  studion runt lunch och fram tills tidig kväll, inte riktigt 8-5 precis, men sen blir det mycket resor också, som nu till exempel.
</p>

<p>
Så mycket längre hann vi inte innan PR-kvinnan kommer in och talar om att våra exakt tjugo minuter är slut,  fast jag hann fixa en autograf i alla fall.
</p>