<p>
Du befinner dig på vinden i ett ganska gammalt hus. På väggen sitter tre stycken strömbrytare på rad, under varje brytare finns en liten skylt med texten "Från" och ett nummer: 1, 2 eller 3. Ovanför sitter likadana skyltar men med texten "Till". Alla brytarna är i läge "Från".<br />
Nere i källaren sitter tre stycken glödlampor, märkta A, B och C. Varje lampa är kopplad till en strömbrytare på vinden.
</p>

<p>
Du får bara gå ner i källaren en gång, om du vill får du gå tillbaks till vinden igen men du får alltså inte gå tillbaks ner om du en gång varit i källaren.<br />
Nu är ditt uppdrag att ta reda på hur strömbrytarna är kopplade till lamporna. 
</p>

<div id="riddle_answer">
<h1>Lösningen är rätt simpel, faktiskt</h1>
<ol>
<li>Slå på brytare 1 och vänta i fem minuter</li>
<li>Slå av brytare 1 och tänd brytare 2</li>
<li>Gå ner i källaren</li>
</ol>
<p>
Den lampan som är tänd tillhör brytare 2, den som är släckt men fortfarande varm tillhör brytare 1 och den tredje, som är släckt och kall tillhör brytare 3.
</p>
