<h2>Stenhård</h2>
Trådarna är väldigt seriösa!

<p>
  Här gäller det att du är saklig och kan argumentera för dina åsikter! Ska du komma med påståenden, till exempel att 50% av alla invandrare begår brott så ska du kunna säga varifrån du fick den informationen! <br />
  Off Topic, chatt eller liknande tolereras inte och allting som vi tycker inte tillför debatten någonting tar vi bort.<br />
</p>

<p>
Bedömmer vi att en tråd är oseriös eller inte leder till debatt enligt  nivån på kategorin så ger vi oss själva rätten att flytta den till en  mer passande del av forumet. <br />
</p>

<p>
  Vi ger oss rätten att gå in och ändra inlägg för att ta bort omogenheter eller lägga upp texten så<br />
  att det är enklare för andra att läsa (rättstavning, styckesinledning).
</p>

<p>
Exemplen förklarar bara väldigt enkelt vad som inte är tillåtet, läs <a href="http://www.hamsterpaj.net/artiklar/?action=show&id=75">Forumsreglerna</a> för att förstå varför bättre.
</p>

