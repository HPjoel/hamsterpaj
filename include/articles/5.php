<h2>Det här behöver du</h2>
<ul>
<li>En tång eller en slö sax</li>
<li>En hundralapp (femhundring är häftigare)<li>
<li>En skvätt T-röd</li>
</ul>

<h2>Så här gör du</h2>
<ol>
<li>Häll upp lika mängder T-röd och vatten i en skål.</li>
<li>Doppa sedeln i blandingen och lyft upp den med tången</li>
<li>Håll sedeln minst en halvmeter bort från skålen och tänd eld i sedelns nedre del.</li>
</ol>

<h2>Tips</h2>
<ul>
<li>Testa med en bit vanligt papper innan du riskerar dina pengar</li>
<li>Mer T-röd ger bättre effekt, men tar man för mycket kan pengarna gå upp i rök</li>
<li>Som alltid när det gäller eld: flamsa inte, håll koll på håret och se upp för gardiner och liknande.</li>
</ul>

<h2>Varför brinner inte pengarna upp?</h2>
<p>
Alkoholen lägger sig utanpå vattnet eftersom det är flyktigare och har lägre ytspäning. Vattnet fungerar alltså som ett sorts isoleringsskikt mellan elden och sedeln.
</p>

