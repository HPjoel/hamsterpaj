<h2>Varför blir det så här?</h2>
<p>
Vi vet inte riktigt varför faktiskt. Vi har suttit och läst en hel del på nätet utan att hitta något riktigt bra svar.
Det troligaste är att ytan på en Mentos består av små, små håligheter som inte är synliga för blotta ögat. Men de är tillräckligt stora för att bilda en "eruption point" som det heter på engelska. En punkt där kolsyran i drickan lösgör sig och bubblar upp.
Det är alltså själva ytan på karamellen som skapar reaktionen, inte innehållet inuti. Vi har testat med mosade Mentos men det fungerar inget vidare.<br />
Har du en riktigt bra förklaring? Skriv i Hamsterpaj-forumet och skicka med info om din källa!
</p>