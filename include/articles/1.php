<h2>Detta behöver du</h2>
<ul>
<li>Coca Cola Light (2L eller 1.5L)</li>
<li>En rulle Mentos</li>
<li>Papprulle från en toarulle eller liknande</li>
<li>Ett pappersark</li>
</ul>

<h2>Så här gör du</h2>
<ol>
<li>Klipp till och tejpa papprullen så att den är lika stor som flaskans öppning, och så att mentos-tabletterna kan falla fritt.</li>
<li>Ställ röret ovanpå pappersarket och häll ner metnos-tabletterna i röret</li>
<li>Öppna cola-flaskan och placera papprullen med tabletterna ovanför, med pappersarket imellan.</li>
<li>Dra bort pappret, så att tabletterna faller ner i light-colan</li>
</ol>

