</p>

<h2>Du behöver</h2>
<ul>
<li>Ett helt vanligt A4-papper, men gärna lite grövre papperstjocklek</li>
</ul>
</p>


<h2>Så här gör du</h2>
<ol>
<li>Vik pappret på mitten.</li>

<li>Vik in övre hörnen av papperet mot mitten.</li>

<li>Fortsätt med att vika in de de nya hörnen mot mitten.</li>

<li>Vik ihop papperet igen längs mittenvikningen du gjorde i början.</li>

<li>Dags att skapa vingar, det gör du genom att vika ner den övre kanten mot den nedre.</li>

<li>Lite tillsnyggning och sen är det bara att prova att kasta</li>

</ol>

