<p class="ingress">
Spray, vax, gelé, lera och mousse är några av alla produkter som finns för att fixa en schysst frisyr, men vad är genetligen bäst - och hur vet man vad man ska välja? Är prdukten för tusentals kronor kilot bättre än den i hyllan på Willys?<br />
Vi bestämde oss för att testa och ta reda på hur det egentligen ligger till.
</p>

<h2>Hur vi gick till väga</h2>
<p>
<ul>
<li>
<a href="/traffa/profile.php?id=3">Johan</a>, medellångt (ca. 8cm) hår som helst står åt alla håll och kanter
</li>
<li>
<a href="/traffa/profile.php?id=57100">Ace</a>, incestlockar och backslick för hela slanten.
</li>
<li>
<a href="/traffa/profile.php?id=47848">Fast</a>, långt hår som ska ligga bra och ha volym
</li>
<li>
<a href="/traffa/profile.php?id=202894">R3my</a>, ungefär som Johans hår, fast orange och lättare att forma
</li>
</ul>
samlades i Nordstan, Göteborg i mitten av Februari 2007. Först tog vi oss till KICKS där vi köpte tre vaxer och två geléer för mellan 50-80kr. Därefter tog vi oss till NK, fåfängans högborg. Två vaxer, två geléer och åttahundra kronor senare satt vi på spårvagnen mot Opaltorget och Willys. Vi hittade en gelé och en burk vax från Eldorado.
</p>
<p>
Väl hemma hos Fast var det dags att börja testa. Vi delade in testet i sex delar:
<ol>
<li>
<h3>Lukt</h3>
Vi kletade ut en liten klick av varje produkt, lät den vandra laget runt och alla fick tycka till om lukten.
</li>
<li>
<h3>Hur lätt det gick att arbeta in produkten i håret</h3>
Vi tog en liten klick av produkten i handen, gnuggade ut den och försökte arbeta in den i håret. En del sjönk direkt in i håret, andra klibbade fast i händerna.
</li>
<li>
<h3>Formbarhet</h3>
När vi väl hade fått in vaxet eller gelén i håret skulle vi forma en frisyr.
</li>
<li>
<h3>Värmetestet</h3>
En del vaxer och geléer smälter på dansgolvet,  resultatet blir en ful frisyr och glansig panna. Vi värmetestade produkterna med två minuter i bastun.
</li>
<li>
<h3>Mekanisk påverkan</h3>
På IKEA har dom små robotar som öppnar och stänger köksluckorna för att testa hur bra dom håller. Vi tog på och av oss hood-tröjor fyra gånger för att se hur bra frisyren höll, och om den gick att återforma efteråt.
</li>
<li>
<h3>Ursköljning</h3>
Hur bra går det att skölja ur produkten ur håret? Biter den sig fast och lämnar en fet yta på hårbottnen eller rinner den av lätt?
</li>
</ol>
</p>

