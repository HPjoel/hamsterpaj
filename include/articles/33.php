Av allt nödvändigt know-how man kan tänkas lära sig är slipsknytandet ett av de mer elementära. Nog för att pappa säkert kan knyta slipsen åt dig men någon gång är det väl dags att lära sig dra åt snaran själv? Okey, då kör vi!
<p>
Den knut vi skall lära oss kallas för four-in-hand och är en av de två vanligaste och enligt många även den snyggaste och mest funktionella då den går att variera ganska mycket. Variation är dock något man kan vänta med tills grunden sitter som den ska.
</p>
<br>
<p>
<em>
Stå framför en spegel och lägg slipsen runt halsen som på bilden (illustrationerna motsvarar din spegelbild).
</em>
</p>
