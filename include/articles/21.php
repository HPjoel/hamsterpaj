<h3>Ett bidrag från <a href="/traffa/profile.php?id=263023">fesoj</a></h3>
<h2>Nära besläktad med myrsloken</h2>
<div class="illustration">
<img src="http://images.hamsterpaj.net/article_illustrations/kebabdjur/fesoj.jpg"   />
Ett fullvuxet kebabdjur, enligt fesoj
</div>
<p>
Kebabdjuret lever på savannen i Afrika där den till största delen livnär sig på kebab och termiter. Kebabdjurets närmaste släkting är myrsloken. Kebabdjuren lever i mindre flockar med 3-6 kreatur. Kebabdjuret är ett skyggt djur och kan känna små rörelser i marken på mycket långt håll, ett kebabdjur kan uppfatta en mäniskas fotsteg på 3-4 mils avstånd.
Kebabdjurets värsta fiende är människan som ofta äter djuret tillsamans med bröd, sallad och mycket vitlökssås.
Djuret kan röra sig i upp till 30km/h.
</p>

