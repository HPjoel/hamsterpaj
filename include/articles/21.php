För en tid sedan anordnade vi på Hamsterpaj en kebabdjurstävling, där vi bad våra besökare skicka in bilder och texter om det mystiska kebabdjuret. Tävlingen är avslutad, men vi publicerar de bästa bidragen här. Vill du se alla bidrag som skickades in i kebabdjurstävlingen så hittar du dom i <a href="http://www.hamsterpaj.net/forum_new/index.php?action=view_thread&thread_id=92102">forumtråden</a>
</p>

<h2>Upptakt</h2>
<p>
Johan här på Hamsterpaj hade ryktesvägen hört talas om kebabdjuret flera gånger under hösten 2006. Små fragment som andra oförsiktigt låtit sippra ut under påverkan av ädla drycker (<em>Jelzin, Saranoff</em>). I korthet förklarade ryktet varifrån all kebab kom, djuren som sedemera blev kebab kallades "kebabdjur", de hade tre ben som de använde för att skutta fram över savannen i afrika.<br />
För att bringa klarhet lanserade vi kebabdjurstävlingen.
</p>

